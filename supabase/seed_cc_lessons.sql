# 第1章 なぜ安全衛生か ─ 災害の重さとハインリッヒの法則

## 1.1 一件の事故が、奪うもの

労働災害は、当人だけの不運ではない。

- 受けた人は、痛みを負い、働けなくなり、生活が変わる
- 家族や同僚も、負担と不安を抱える
- 職場は、人員の穴・調査・補償・信用の低下を背負う

つまり、個人の不注意の話ではなく、**組織の安全衛生の問題**だ。だから法律は、事業者に危険を防ぐ義務を課している。一件の重大災害の本当のコストは、その場の被害の何倍も大きい。

一つの事故は、波紋のように広がる。直接けがをした人だけでなく、その穴を埋める同僚、調査と補償にあたる担当、再発を恐れる現場全体に影響が及ぶ。受けた人が長く休めば、その間の業務をほかの人が背負う。残った人は「次は自分かもしれない」と身構え、危ない作業を避けるか、逆に慣れで麻痺する。声の出にくい職場では、ヒヤリも事故も隠れる。表に出た一件の裏で、職場は静かに削られていく。

逆に言えば、災害を防ぐことは、仕事を萎縮させることではない。安心して手を動かせる場をつくることだ。安全な現場は、生産性の土台になる。守りの研修に見えて、実は攻めの土台づくりでもある。

## 1.2 ハインリッヒの法則 ─ 一件の重大の裏に、三百のヒヤリ

労働安全の古典的な経験則に、ハインリッヒの法則がある。一件の重大な災害の背後には、二十九件の軽い災害があり、その背後には三百件の、ケガには至らなかった「ヒヤリとした・ハッとした」出来事がある——という比率だ。

```diagram-triangle
重大な災害(1)
軽微な災害(29)
ヒヤリ・ハット(300)
重大の裏には、無数のヒヤリが隠れている
```

この三角形が示すのは、二つのことだ。

- 重大災害は、突然ではない。同じ原因のヒヤリが、その前に何度も起きている
- 重大だけを追っても、減らせない。**土台のヒヤリを潰すこと**が、頂点を防ぐ

数字そのものより、構造をつかむことが大事だ。頂点(重大)は氷山の一角で、見えにくい土台(ヒヤリ)が圧倒的に多い。そこに手を入れれば、まだケガが出ていない段階で、原因を断てる。一般的な見解では、ヒヤリの段階で対策を打つことが、最も費用対効果が高いとされる。

逆に言えば、安全衛生とは、事故が起きてから原因を探す活動ではない。**事故になる前のヒヤリを拾い、止める**活動だ。守りに見えて、現場の働きやすさと生産性を支える土台でもある。


# 第2章 労働災害の型 ─ どこで人は傷つくか

## 2.1 災害には、決まった「型」がある

災害は、ばらばらに起きるように見えて、原因の形(事故の型)で整理できる。厚生労働省は、墜落・転落、転倒、はさまれ・巻き込まれ、激突、飛来・落下、交通事故など、二十あまりの型に分類している(2026年時点)。型で見ると、自分の現場に多い危険が浮かび上がる。

```diagram-numbered
title: 現場で多い災害の型
1. 転倒(つまずき・すべり・段差)
2. 腰痛など(動作の反動・無理な動作)
3. 墜落・転落(高所・脚立・階段)
4. はさまれ・巻き込まれ(機械・扉・台車)
5. 交通事故(移動・配送・通勤)
6. 熱中症(高温環境・夏季の屋内外作業)
```

近年の統計でも、休業を伴う災害で最も多いのは転倒、次いで腰痛など動作の反動・無理な動作、墜落・転落と続く。死亡災害では墜落・転落、交通事故、はさまれ・巻き込まれが上位を占める(厚労省 令和6年労働災害発生状況、2026年時点)。熱中症による死亡は近年大幅に増えており、注意が要る型として加わっている。

## 2.2 型ごとの「止めどころ」

型がわかれば、止める場所もわかる。

- **転倒**:足元の水・油・コードを片づける。段差を見える化する。急がない・両手をふさがない
- **腰痛**:重い物は一人で持たない。膝を使って持ち上げる。台車・補助具を使う。同じ姿勢を続けない
- **墜落・転落**:脚立は正しく開いて固定し、天板に乗らない。高所では手すり・安全帯。階段は手すりを持つ
- **はさまれ・巻き込まれ**:動いている機械に手を入れない。停止・電源遮断を確認してから触れる。袖・手袋の巻き込みに注意
- **交通事故**:時間に追われた運転をしない。スマホを見ない。点検と休憩を組み込む
- **熱中症**:水分・塩分をこまめに。休憩と日陰を確保。体調の異変は隠さず申し出る

どの型にも共通するのは、「気をつける」だけに頼らないことだ。片づける・分ける・止めてから触れる・道具を使う——**動作ではなく環境と手順で**、危険を先に潰す。具体の基準・手順は、自社の規程と専門家に従う。

注意したいのは、最も多い型ほど「ありふれて見える」点だ。転倒や腰痛は、特別な高所作業や機械作業より地味で、「自分には関係ない」と見過ごされやすい。だが統計上、休業を伴う災害で上位を占めるのは、まさにこの地味な型だ。派手な危険ほど警戒され、地味な危険ほど放置される——その油断が、件数の差になって表れる。だからこそ、足元・持ち方・段差といった日常の動作に、最初に目を向ける価値がある。


# 第3章 ヒヤリハットとKY ─ 危険を、起きる前に見つける

## 3.1 ヒヤリハットは、未来の事故の予告

ヒヤリハットとは、ケガには至らなかったが「ヒヤリとした・ハッとした」出来事をいう。第1章の三角形でいえば、土台の三百にあたる。

- ヒヤリは、まだ被害が出ていない「無料の警告」だ
- 一つのヒヤリを共有すれば、同じ原因の事故を、皆で避けられる
- 隠せば、同じ場所で誰かが、今度は本当にケガをする

大切なのは、ヒヤリを**責めずに集める**ことだ。報告した人を責める職場では、ヒヤリは隠される。隠れたヒヤリは、止められない。

```diagram-flow
ヒヤリに気づく(危なかった、と感じる)
記録して共有する(日時・場所・状況)
原因を皆で考え、対策を打つ
```

報告は、犯人探しではない。学びの入り口だ。「危なかった」を口に出せる職場ほど、重大災害から遠ざかる。

## 3.2 KY(危険予知)― 作業の前に、危険を先取りする

KY(危険予知)は、作業を始める前に、ひそむ危険を皆で出し合い、対策を決める活動だ。中央労働災害防止協会が広めた危険予知訓練(KYT)の基礎は、四つの段階で進む(2026年時点)。

```diagram-numbered
title: 危険予知の4ラウンド
1. 現状把握 ── どんな危険がひそんでいるか
2. 本質追究 ── これが危険のポイントだ
3. 対策樹立 ── あなたならどうする
4. 目標設定 ── 私たちはこうする
```

- **第1ラウンド(現状把握)**:作業の中に、どんな危険があるかを挙げる
- **第2ラウンド(本質追究)**:挙がった中で、特に危ない要点を絞り込む
- **第3ラウンド(対策樹立)**:その危険を避けるには、どうするかを出し合う
- **第4ラウンド(目標設定)**:実行する対策を、行動目標として皆で決める

KYは、特別な道具がなくても、作業前の短いミーティングでできる。「今日のこの作業、どこが危ないか」を一言ずつ出すだけでも、危険の先取りになる。

## 3.3 指差呼称 ― 「気をつける」を、行動に変える

危険を頭でわかっていても、人は省略する。それを防ぐのが指差呼称だ。対象を指で差し、声に出して確認する。

- 「○○、ヨシ!」と、目・指・声を一致させる
- 確認の動作を体に入れることで、思い込みや見落としが減る
- 一般に、指差呼称はヒューマンエラーの抑制に有効とされる(2026年時点)

「気をつけます」は、あいまいだ。**指で差して声に出す**——その一動作が、確認を確実にする。


# 第4章 4S ─ 安全の土台をつくる

## 4.1 4Sとは

4Sは、整理・整頓・清掃・清潔の頭文字をとった、職場環境の基本だ。安全衛生のあらゆる活動の土台になる。

```diagram-layers
foundation: 整理・整頓(要る物と要らない物を分け、定位置に置く)
top: 清掃(ゴミ・汚れを取り除く) | 清潔(きれいな状態を保ち続ける)
```

- **整理**:要る物と要らない物を分け、要らない物を捨てる
- **整頓**:要る物を、決めた場所に、決めた量だけ置く。すぐ取り出せるようにする
- **清掃**:ゴミ・ほこり・油などを取り除き、問題点が見える状態にする
- **清潔**:整理・整頓・清掃した状態を、保ち続ける

(2026年時点。中災防・厚労省の整理による)

## 4.2 なぜ4Sが、安全につながるか

片づけは、見た目の問題ではない。災害の原因を、先に消す動作だ。

- 床に物がなければ、つまずいて転ばない(転倒の予防)
- 通路が空いていれば、台車や避難の動線が確保される
- 物が定位置にあれば、無理な姿勢で取らずに済む(腰痛の予防)
- きれいな床は、こぼれた油や水の異常にすぐ気づける

4Sの効いた職場は、危険が「見える」。乱れた職場は、危険が物に紛れて「見えない」。**4Sは、危険を見えるようにする活動**だと言える。清掃の効果は、薬剤の強さより、こまめさで決まることが多い。


# 第5章 作業環境と健康管理の基本

## 5.1 災害は「人」だけでなく「環境」からも起きる

危険は、不注意だけが原因ではない。作業環境そのものが、人を傷つける。

- **暑さ・寒さ**:高温多湿は熱中症を招く。水分・塩分・休憩・日陰を仕組みで確保する
- **照明**:暗い・まぶしい環境は、見落としと転倒を生む
- **騒音・粉じん・化学物質**:長く浴び続けると、健康を損なう。換気・保護具・取り扱い手順を守る
- **長時間・無理な姿勢**:疲労と腰痛の原因。作業の分担・休憩・道具で和らげる

環境のリスクは、個人の頑張りでは消せない。**設備・手順・体制**で対処する領域だ。具体の基準・測定・対策は、自社の安全衛生委員会と専門家に従う。

## 5.2 自分の体調も、安全の一部

どれだけ環境を整えても、本人の状態が悪ければ事故は起きる。

- 睡眠不足・体調不良・過度の疲労は、判断と反応を鈍らせる
- 体調の異変(めまい・頭痛・強い疲労)は、隠さず申し出る
- 健康診断・ストレスチェックは、不調を早く見つける仕組み(メンタル面はCC-04で詳述)

「無理を押して働く」ことは、美徳ではない。本人と周囲を危険にさらす。**調子が悪いときに言える職場**が、安全な職場だ。

人の集中力には波がある。疲れがたまる時間帯、慣れて気がゆるむ作業、急かされて手順を飛ばす場面——事故は、その隙間に入り込む。ベテランほど「いつもやっているから」と確認を省きやすく、新人ほど「危ないと言い出しにくい」。どちらの落とし穴も、本人の努力だけでは埋まらない。だから、休憩を仕組みで挟む、危険を声に出せる空気をつくる、慣れた作業ほど指差呼称で確認に戻す——個人の気力ではなく、職場の設計で支える。それが、長く続く安全のかたちだ。


# 第6章 事故が起きたときの初動と報告

## 6.1 初動の優先順位

備えても、事故は起こりうる。そのときの初動が、被害の大きさを分ける。

```diagram-flow
人命優先(救護・119番・二次災害の防止)
状況の保全と関係者への連絡(上長・安全担当)
記録と報告(何が・いつ・どこで・どう起きたか)
```

- **第一に、人命**:けが人の救護、必要なら119番。周囲の二次災害(感電・転落・有害物)を防ぐ
- **次に、連絡**:上長・安全担当に速やかに伝える。自分一人で抱えない
- **そして、記録**:日時・場所・状況・目撃者を、できるだけ早く残す

慌てて現場を片づける前に、まず人を守り、状況を伝える。順番を間違えないことが、初動の要だ。

## 6.2 報告は、責めるためではなく、再発を防ぐため

事故やヒヤリの報告で、最も静かで深い失敗は、「大ごとにするな」ともみ消すことだ。

- 報告がなければ、組織は原因に気づけない
- 気づけない原因は、また同じ事故を起こす
- 「報告したら責められる」職場では、危険が隠れ続ける

報告は、犯人探しではなく、同じ事故を防ぐための学びだ。組織は責めない仕組みをつくり、現場は迷ったら報告する。**報告のあるところに、改善が積み上がる**。具体の報告ルート・様式は、自社の規程に従う。


# 第7章 反復ワーク

## 7.1 自分の現場のヒヤリを、書き出す

直近で「危なかった」と感じた場面を、一つ思い出して書く。

- どこで、どんな状況で、何が危なかったか
- それは、第2章のどの型(転倒・腰痛・墜落・はさまれ・交通・熱中症)に近いか
- 同じ危険を消すには、環境や手順を、どう変えればよいか

責めるためではなく、未来の事故を一つ消すために書く。

## 7.2 作業前のKYを、一回まわす

明日の作業を一つ選び、四つのラウンドを紙の上で回す。

- 現状把握:どんな危険がひそむか
- 本質追究:特に危ない要点はどこか
- 対策樹立:どうすれば避けられるか
- 目標設定:今日、自分(たち)はこうする

頭の中で済ませず、一言でも書く。書くと、見えていなかった危険が浮かび上がる。


