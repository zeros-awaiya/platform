# 第1章 ハラスメントとは何か

## 1.1 なぜ「職場の問題」なのか

ハラスメントは、当人同士のいざこざではない。

- 受けた人は、心身を壊し、力を発揮できなくなる
- 周りも萎縮し、職場全体の士気が落ちる
- 放置した組織は、法的責任・人材流出・信用失墜を負う

つまり、個人の相性の話ではなく、**組織の安全衛生の問題**だ。だから法律は、事業主に防止の義務を課している。

一つの言動が、波紋のように広がる。直接の当事者だけでなく、それを見ていた人、相談を受けた人、噂で聞いた人——職場のあちこちに影響が及ぶ。受けた人が辞めれば、その穴を埋める採用と教育に、新たな費用がかかる。残った人は「次は自分かもしれない」と身構え、挑戦をやめる。声が出にくい職場では、ミスや不正も隠れる。ハラスメントの本当のコストは、表に出た一件の何倍も大きい。

逆に言えば、ハラスメントを防ぐことは、我慢を強いることではない。**安心して意見が言える場をつくる**ことだ。安全な場は、生産性の土台になる。守りの研修に見えて、実は攻めの土台づくりでもある。

## 1.2 パワハラの3要素

職場のパワーハラスメントは、次の三つを**すべて満たすもの**と定義される（労働施策総合推進法）。

```diagram-triangle
優越的な関係を背景とした言動
業務上必要かつ相当な範囲を超えている
就業環境が害される
三つすべてが揃ったとき、パワハラが成立する
```

ここが線引きの起点になる。

- **優越的な関係**:上司と部下に限らない。経験・知識・人数など、抵抗しにくい関係すべて
- **必要かつ相当な範囲を超える**:指導に必要な限度を超えているか。手段・回数・場が過剰か
- **就業環境が害される**:受けた人が働きづらくなったか

逆に言えば、業務上必要で、手段が相当な範囲なら、それは**指導であってパワハラではない**。三要素は、指導を萎縮させないための線でもある。

ここを誤解すると、二つの方向に転びやすい。一つは「何を言ってもハラスメントと言われる」と恐れて、必要な指導まで避けてしまう萎縮。もう一つは「指導のためなら何をしてもいい」と、手段の過剰に無自覚になる暴走。三要素は、その両極のあいだに立つための物差しだ。必要なことを、相当な手段で、相手の働く場を壊さずに伝える——その三つが揃えば、強い指導も成り立つ。

注意したいのは、判断の重心が**受けた側の就業環境**に置かれている点だ。言った側の意図が「指導のつもり」でも、受けた側が働きづらくなり、その受け止めが社会通念上もっともだと言えるなら、線を越えている可能性がある。「そんなつもりはなかった」は、出発点にはなるが、免罪符にはならない。


# 第2章 6類型と、グレーゾーンの線引き

## 2.1 パワハラの6類型

厚生労働省は、代表的な型を6つに整理している。

```diagram-numbered
title: パワハラの6類型
1. 身体的な攻撃（暴行・傷害）
2. 精神的な攻撃（脅迫・侮辱・ひどい暴言）
3. 人間関係からの切り離し（隔離・無視・仲間外し）
4. 過大な要求（不可能な業務の強制・妨害）
5. 過小な要求（能力とかけ離れた仕事・仕事を与えない）
6. 個の侵害（私的なことへの過度な立ち入り）
```

①②は分かりやすい。見落としやすいのは③〜⑥だ。「無視する」「閑職に追いやる」「家庭の事情をしつこく聞く」——強い言葉を使わなくても、ハラスメントは成立する。

具体の場面に置くと、見えてくる。

- ③ 切り離し:一人だけ会議に呼ばない。挨拶を返さない。チャットで意図的に外す
- ④ 過大な要求:終わるはずのない量を期限つきで押しつける。新人に説明なく難業務を丸投げ
- ⑤ 過小な要求:管理職に延々と草むしりだけをさせる。仕事を一切与えず放置する
- ⑥ 個の侵害:交際・家族・病歴をしつこく詮索する。私物やSNSを無断で覗く

④と⑤は、一見正反対だ。だが根は同じ——**業務の必要性ではなく、相手を追い込むために仕事が使われている**。量が問題なのではなく、向きが問題なのだ。

## 2.2 指導とハラスメントを分ける問い

同じ「ミスを注意する」でも、線の内と外がある。

- **何を**:行動・事実を指摘しているか／人格を否定していないか
- **どこで**:必要な場か／大勢の前でさらしていないか
- **どれだけ**:一度伝えれば足りるか／執拗に繰り返していないか
- **どう**:改善のためか／感情をぶつけているか

「相手の成長のため」に向いていれば指導。「相手を責める・支配する」に向いていれば、ハラスメントに傾く。迷ったら、この四つの問いに戻る。

たとえば、同じ「書類のミスを指摘する」場面を考える。席で短く「ここ数字が違うから直して」と伝えるのは指導だ。だが、フロア全体に聞こえる声で「何度言えば分かるんだ、これだから君は」と人格に踏み込み、それを毎日繰り返せば、同じ出発点でも線の外に出る。**何を・どこで・どれだけ・どう**——この四つのどこかが過剰に振れたとき、指導は静かにハラスメントへ変わる。

## 2.3 グレーゾーンは、消えない

線引きをしても、白とも黒とも言えない場面は残る。

- 受け取り方には個人差がある
- 関係性や文脈で、同じ言動の意味が変わる
- 「これくらい」の感覚は、世代や立場でずれる

グレーを完全になくすことはできない。だからこそ、**一人で決めない**。迷う言動は、口に出す前に同僚や上長に相談する。その一手間が、踏み越えを防ぐ。


# 第3章 セクハラ・マタハラ・その他

ハラスメントは、パワハラだけではない。性別・妊娠・出産・育児・介護にかかわる嫌がらせも、法律で防止が義務づけられている。共通するのは、相手の意に反し、働く環境を損なうという構造だ。型は違っても、見るべき軸は同じだ。

パワハラ以外にも、防止が義務づけられたハラスメントがある。

- **セクシュアルハラスメント**:性的な言動で就業環境を害する。対価型（拒否で不利益）と環境型（不快な言動）。同性間・異性間どちらも成立
- **妊娠・出産・育児・介護に関するハラスメント**:制度利用や状態を理由とする嫌がらせ・不利益取扱い
- **顧客・取引先からの著しい迷惑行為（カスハラ）**:別コース（CC-07）で詳述

共通するのは、**「相手が望まない」「就業環境を害する」**という軸だ。「冗談のつもり」「親しみのつもり」は、受けた側の痛みを打ち消さない。

セクハラで誤解されやすいのは、「身体に触れなければ大丈夫」という思い込みだ。容姿や交際についての発言、性的な冗談、執拗な食事の誘いも、環境型のセクハラになりうる。相手が立場上断りにくい関係なら、なおさらだ。

マタハラ・育介ハラも、悪意のない形で起きやすい。「忙しい時期に休まれると困る」「子どもがいるなら昇進は無理だろう」——気遣いや常識のつもりの一言が、制度利用をためらわせ、不利益な扱いにつながる。制度は、使う人を責めるためにあるのではない。使える前提で職場を回す——それが管理の側の仕事になる。


# 第4章 受けた側・見た側の動き方

ハラスメントは、当事者だけでは止まりにくい。受けた人は声を上げにくく、周りは関わりを避けがちだ。その二つの沈黙が重なったとき、ハラスメントは長く続く。だから、受けたとき・見たとき、それぞれに「最初の一手」を知っておく。

## 4.1 受けたとき

一人で抱え込まないことが、最初の一歩。

```diagram-flow
記録する（日時・場所・言動・目撃者）
信頼できる人・相談窓口に相談する
会社の窓口・公的窓口へつなぐ
```

- **記録**:いつ・どこで・誰が・何を、をメモや録音で残す
- **相談**:社内の相談窓口、人事、労働組合、または公的窓口（総合労働相談コーナー等）
- 我慢は解決にならない。早く声を上げるほど、選べる手は多い

記録は、特別なものでなくていい。手帳の隅、スマホのメモ、自分宛のメール。事実を、起きたその日に短く残す。記憶は薄れ、上書きされる。だが、日付の入った記録は残る。これは、誰かを陥れるためではない。自分の身に起きたことを、自分が正確に説明できるようにするための備えだ。

「これくらいで相談していいのか」とためらう人は多い。だが、窓口は白黒をつける裁判所ではない。**迷いの段階で話していい場所**だ。早い相談ほど、こじれる前に手を打てる。

## 4.2 見たとき

周囲の沈黙が、ハラスメントを支えてしまう。

- 見て見ぬふりをしない（黙認は容認になる）
- 受けた人に声をかける（「大丈夫?」の一言が孤立を防ぐ）
- 自分が止めにくければ、相談窓口・上長へつなぐ

傍観者が動くだけで、職場の空気は変わる。直接対決でなくていい。つなぐことが、行動だ。


# 第5章 管理職の措置義務（パワハラ防止法）

> 第5章は、部下を持つ管理職・相談窓口担当が対象。

事業主には、ハラスメント防止の措置が法的に義務づけられている（労働施策総合推進法。中小企業も2022年4月から全面義務化）。管理職は、その実務の最前線に立つ。

```diagram-layers
foundation: 方針の明確化と周知（許さない／処分する旨）
top: 相談体制の整備 | 事後の迅速・適切な対応 | プライバシー保護・不利益取扱いの禁止
```

- **方針の周知**:何がハラスメントか、許さない姿勢、処分の方針を全員に伝える
- **相談対応**:相談を受けたら、軽視せず、事実を確認し、放置しない
- **二次被害の防止**:相談者・協力者のプライバシーを守り、相談したことで不利益に扱わない
- **再発防止**:個別対応で終わらせず、職場の構造（業務量・関係性）に目を向ける

管理職自身が、最大の「優越的関係」を持つ。だからこそ、自分の言動に最初に線を引く立場でもある。

相談を受けたときの初動が、その後を大きく分ける。やってはいけないのは、「気にしすぎだ」「あの人も悪気はない」と、その場で結論を出してしまうことだ。それは相談者を二度傷つけ、窓口への信頼を壊す。まずは、評価を挟まずに事実を聴く。「話してくれてありがとう」と受け止める。そのうえで、自分一人で抱えず、人事や正式な窓口へつなぐ。

公平さも欠かせない。一方の話だけで加害者を決めつけるのも、また別の問題を生む。事実を確認し、双方の言い分を聴き、プライバシーを守りながら進める。**相談者を守ること**と**手続きを公正に保つこと**は、両立させなければならない。難しいからこそ、組織の仕組み（窓口・規程・第三者）に乗せる。


# 第6章 ありがちな失敗

熟練した人でも通る道。気づいて直せればいい。大切なのは、自分は大丈夫だと思い込まないことだ。

**失敗① 「指導だから」で押し切る**

目的が正しくても、手段が過剰なら線を越える。「成果を出させるため」「本人のため」という大義は、手段の暴走を覆い隠しやすい。目的の正しさと、手段の相当性は、別々に点検する。迷ったら、何を・どこで・どれだけ・どう、の四つに戻る。

**失敗② 「冗談・親しみ」で片づける**

長い付き合いや砕けた関係は、油断を生む。「あいつとは仲がいいから」は、言った側の感覚にすぎない。判断は意図ではなく、受けた側の就業環境でなされる。親しさは、踏み込んでよい理由にはならない。

**失敗③ 相談を受けて、もみ消す**

「大ごとにするな」「穏便に」は、最も静かで深い失敗だ。相談を軽く扱えば、二次被害になり、職場は「言っても無駄」を学習する。記録し、評価を挟まず受け止め、正式な窓口へつなぐ。

**失敗④ 沈黙する周囲**

傍観は、結果として容認になる。直接止めるのが難しくても、受けた人に声をかける、窓口へつなぐ——間接的な行動はいくつもある。沈黙だけが、何もしない選択だ。

**失敗⑤ 一人で抱える**

加害も被害も、孤立の中で深まっていく。「自分がおかしいのかも」「騒げば立場が悪くなる」——その思い込みが、声を止める。だからこそ、迷いの段階で相談する。一人で判定しない。それが、踏み越えと我慢の、両方を防ぐ。


# 第7章 反復ワーク

## 7.1 自分の一言を、振り返る

直近で部下・同僚に強く言った場面を一つ思い出し、書き出す。

- それは「行動」を指摘したか、「人格」を否定したか
- 場・回数・手段は、相当な範囲だったか
- 相手の成長に向いていたか、相手を責めることに向いていたか

責めるためではなく、線を確かめるために書く。

## 7.2 グレーゾーンを、言語化する

「これはハラスメントか?」と迷った場面を一つ挙げ、なぜ迷うのかを書く。書いて、相談相手に見せる。一人で判定しないための練習だ。


