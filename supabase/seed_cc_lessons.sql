# 第1章 カスタマーハラスメントとは何か

## 1.1 正当なクレームと、何が違うのか

クレームそのものは、悪ではない。商品やサービスへの不満の表明は、改善の入り口になる。問題は、要求の中身ではなく、その伝え方が一線を越えたときに起きる。

職場におけるカスタマーハラスメントは、一般に次の三つを**すべて満たすもの**として整理されている（厚労省 カスタマーハラスメント対策企業マニュアル等）。

```diagram-triangle
顧客・取引先など、事業に関係する者による言動
社会通念上、許容される範囲を超えている
労働者の就業環境が害される
三つが揃ったとき、カスタマーハラスメントとされる
```

ここが線引きの起点になる。

- **誰が**:顧客に限らない。取引先、施設の利用者など、事業に関係する者の言動が対象
- **どこまで**:社会通念上、許容される範囲を超えているか
- **何が起きるか**:受けた従業員の就業環境が害されるか

正当なクレームは、要望の中身に妥当性があり、伝え方も相当な範囲にとどまる。そこを越えたとき、対応の相手は「お客様」から「対処すべき相手」へと、性質が変わる。

ここを誤解すると、二つの方向に転びやすい。一つは「お客様の言うことだから」と、過剰な言動まで受け入れてしまう我慢。もう一つは「面倒な客はカスハラだ」と、正当な苦情まで切り捨ててしまう過剰反応。三要素は、その両極のあいだに立つための物差しだ。正当な要望には応え、越えてきた言動には組織で身を守る——その両方が、同じ線の上にある。

## 1.2 判断の二つの軸

線を引くとき、二つの軸を分けて見る、という考え方が広く示されている。

- **要求の内容に、妥当性があるか**:そもそも、その要求は筋が通っているか。事実に基づくか
- **要求を実現するための手段・態様が、社会通念上、相当か**:伝え方・回数・場が過剰でないか

この二軸で見ると、整理しやすい。要求の中身がもっともでも、伝え方が暴力的なら線を越える。逆に、要求の中身そのものが著しく妥当性を欠く場合は、手段がどうであれ不相当とされやすいと考えられる。

注意したいのは、判断の重心が**受けた側の就業環境**に置かれている点だ。客の側に「正当な抗議のつもり」があっても、手段が過剰で従業員が働きづらくなり、その受け止めが社会通念上もっともだと言えるなら、線を越えている可能性がある。「客として当然の権利だ」は、出発点にはなるが、免罪符にはならない。


# 第2章 カスハラの類型

## 2.1 「手段・態様」が不相当になる型

要求の中身が妥当でも、伝え方が次のような形になれば、ハラスメントに傾く。厚労省マニュアル等で示される代表的な類型を整理する。

```diagram-numbered
title: カスタマーハラスメントの主な類型
1. 身体的な攻撃（暴行・傷害・物を投げる）
2. 精神的な攻撃（暴言・侮辱・人格否定・脅し）
3. 威圧的な言動・土下座などの不当な要求
4. 継続的・執拗な言動（同じ要求を繰り返す）
5. 拘束的な行動（長時間の電話・居座り・帰さない）
6. 差別的・性的な言動／従業員個人への攻撃
7. SNS・インターネット上での誹謗中傷・晒し
```

①②は分かりやすい。見落とされやすいのは、強い言葉を使わずに人を追い込む型だ。

具体の場面に置くと、見えてくる。

- ③ 不当要求:「誠意を見せろ」と土下座や金銭を求める。権限のないことを強要する
- ④ 継続・執拗:解決済みの件で何度も電話する。同じ説明を繰り返させ続ける
- ⑤ 拘束:営業時間を過ぎても居座る。退店・終話を拒み、担当者を長時間ふさぐ
- ⑥ 個人攻撃:名指しで人格をののしる。容姿・性別に踏み込む。私生活を詮索する
- ⑦ SNS晒し:従業員の顔・名前を無断で投稿し、ネット上で攻撃を煽る

⑤や⑦は、一見「暴力」ではない。だが、根は同じ——**正当な解決ではなく、相手を屈服させることに、手段が向かっている**。要求の中身ではなく、向きが問題なのだ。

特に⑦のSNS・ネット上での攻撃は、現場の外で起き、対応が後手に回りやすい。従業員の顔写真や実名が拡散されると、被害は職場の外まで広がり、本人の私生活や安全をも脅かす。店頭での一件として閉じず、組織として把握し、削除依頼・相談・通報の判断を会社が担う対象になる。「ネットのことだから個人で対処を」と現場に投げない、という線引きが要る。

## 2.2 線が引きにくい場面

白とも黒とも言えない場面は、必ず残る。

- 強い口調の抗議が、正当な指摘なのか、威圧なのか
- 繰り返しの連絡が、確認なのか、執拗な攻撃なのか
- 「責任者を出せ」が、正当な要望なのか、拘束の入り口なのか

グレーを完全になくすことはできない。だからこそ、**一人で判定しない**。迷う場面は、その場で結論を出さず、上長や責任者に共有する。判断を組織に乗せることが、踏み外しと我慢の両方を防ぐ。


# 第3章 一次対応の型 ─ 傾聴と毅然の両立

## 3.1 まず「要望」と「手段」を切り分ける

カスハラ対応の核は、矛盾するように見える二つを、同時に持つことだ。

- **傾聴**:要望の中身には、誠実に耳を傾ける
- **毅然**:過剰な手段には、応じない姿勢をとる

この二つは両立する。要望を聴くことと、暴言や不当要求を受け入れることは、別だからだ。「お話は承ります。ですが、その言い方には応じられません」——中身は聴き、手段は断る。これが一次対応の基本姿勢になる。

傾聴に偏りすぎると、際限のない要求に飲み込まれる。毅然に偏りすぎると、正当な苦情まで突っぱね、火に油を注ぐ。両方を同時に持つのは難しい。だからこそ、個人の話術に頼らず、型として持っておく。型があれば、感情が高ぶる場面でも、立ち戻る場所ができる。

## 3.2 切り分けの手がかり

同じ「強い苦情」でも、対応の向きを分けられる。

- **事実か、感情か**:何が起きたかを確認する。感情の高ぶりには巻き込まれない
- **要求は何か**:相手が本当に求めているもの（交換・返金・説明など）を特定する
- **応じられる範囲か**:自社の規程・権限の内か外か。外なら安請け合いしない
- **手段は相当か**:暴言・拘束・脅しが入っていないか

応じられる要望には応じる。応じられないもの・権限外のものは、その場で約束しない。手段が過剰になったら、対応の段階を変える合図と捉える。

## 3.3 やってはいけない一次対応

- その場しのぎで、できない約束をする(後でより大きな要求を生む)
- 個人の判断で金銭や特別対応に応じる
- 売り言葉に買い言葉で、感情をぶつけ返す
- 一人で抱え、長時間ひとりで対応し続ける

毅然とは、強く出ることではない。**応じられないことを、丁寧に、しかし明確に伝える**ことだ。


# 第4章 エスカレーションの基準

## 4.1 一人で抱えない、の具体化

一次対応の人が、いつ・誰に上げるか。その基準を、あらかじめ言葉にしておく。判断を個人の根性に委ねないことが、現場を守る。

```diagram-flow
一次対応（傾聴＋要望と手段の切り分け）
基準に触れたら、上長・責任者へエスカレーション
組織対応（複数対応・記録・必要に応じ警察/弁護士）
```

## 4.2 上げる合図（一般的な目安）

次のような場面は、一人で続けず、上長・責任者に引き継ぐ合図と捉えられる。

- 暴力・脅迫・器物損壊など、身体や安全への危険があるとき
- 土下座・金銭など、不当な要求がなされたとき
- 長時間の拘束・居座り・繰り返しの連絡が続くとき
- 従業員個人への攻撃・SNS晒しが起きたとき
- 自社の権限・規程を超える要求を求められたとき

危険が差し迫る場合は、社内のエスカレーションを待たず、その場で警察等へ通報する判断もありうる。具体の基準は、自社の対応規程と責任者の指示に従う。

エスカレーションをためらわせるのは、たいてい「これくらいで上げていいのか」という遠慮だ。だが、基準に触れたら上げる、と決めておけば、その遠慮は判断から消える。上げることは、対応力がないことではない。むしろ、組織の仕組みを正しく使えていることだ。早く上げるほど、こじれる前に手を打てる。

## 4.3 引き継ぐときに渡すもの

エスカレーションは、丸投げではない。次の人がすぐ動けるよう、最小限の事実を渡す。

- いつ・どこで・誰が・何を要求しているか
- これまでの対応の経緯（何を伝え、相手がどう反応したか）
- 危険の有無（暴力・脅迫・拘束の要素）


# 第5章 組織で守る仕組み

## 5.1 一人で対応させない

カスハラを個人の対応力の問題にすると、現場は疲弊する。組織で守る、とは仕組みで支えることだ。

- **複数対応**:一定の段階からは、必ず二人以上で当たる
- **時間・場所を区切る**:対応時間・対応場所をあらかじめ決め、際限のない対応を避ける
- **役割を分ける**:対応役と、記録役を分けると、冷静さと正確さを保ちやすい
- **窓口を一本化**:担当者を点々とさせず、組織として一つの対応に揃える

## 5.2 記録の取り方

記録は、誰かを陥れるためではない。起きたことを、組織が正確に把握し、対応を判断するための備えだ。

- **いつ**:日時（開始・終了。拘束時間が分かるように）
- **どこで**:店頭・電話・メール・SNSなど、発生の場
- **誰が**:相手の特定につながる情報（分かる範囲で）
- **何を**:言動の内容を、できるだけ事実のまま。発言は要約せず、可能なら原文に近く
- **どう対応したか**:こちらが何を伝え、相手がどう反応したか
- **証跡**:録音・録画・通話履歴・メール・投稿のスクリーンショットなど(自社の規程の範囲で)

記録は、起きたその場で、短く残す。記憶は薄れ、上書きされる。日付の入った記録だけが残る。

記録が揃っていると、その後の対応の選択肢が広がる。同じ相手による繰り返しのパターンが見え、組織として方針を決めやすくなる。悪質な事案で警察・弁護士に相談する際も、客観的な記録があるかどうかで、打てる手が変わる。記録は、対応した一人を守る証跡であり、組織が判断を誤らないための土台でもある。様式や保存方法は、自社の規程に従って統一しておく。

## 5.3 被害を受けた従業員へのケア

カスハラは、対応した人の心身に傷を残す。

- 対応後に、責めずに話を聴く場を持つ
- 一人で対応させた状態を、放置しない
- 必要なら、配置・担当の調整や専門窓口へのつなぎを検討する

「客あしらいができないお前が悪い」という空気は、二次被害になる。守られると分かっている職場でこそ、毅然とした一次対応ができる。

逆に言えば、組織で守る仕組みは、現場の負担を増やすものではない。複数対応も記録も、いざというときに一人を矢面に立たせないための備えだ。仕組みがあると、現場は「ここまでは応じる、ここからは上げる」を迷わず実行できる。守りの仕組みは、現場の判断を軽くする設計でもある。


# 第6章 管理職・会社の役割

> 本章は、部下を持つ管理職・対応の責任者が対象。

会社には、従業員が安全に働けるよう配慮する立場がある。カスハラ対応は、現場の根性ではなく、会社の責任として設計されるべきものだ。2026年時点、顧客等からの著しい迷惑行為への対策は、雇用管理上の課題として位置づけが強まっている。

- **方針の明確化と周知**:正当な要望には応じ、ハラスメントには毅然と対応する、という会社の姿勢を全員に示す
- **対応手順の整備**:一次対応・エスカレーション基準・記録様式を、あらかじめ決めておく
- **相談体制**:対応した従業員が、ためらわず相談できる窓口を用意する
- **守る姿勢**:従業員を矢面に立たせ続けない。組織として前に出る

管理職がやってはいけないのは、「お客様を怒らせるな」と現場にだけ我慢を求めることだ。それは従業員を孤立させ、離職と信用失墜を招く。**守るべきは、現場で働く人**——この優先順位を、管理職自身が体現する。

公平さも欠かせない。すべての強い苦情をカスハラと決めつければ、正当な要望への対応を見失う。要求の妥当性と手段の相当性を分けて見る——その物差しを、組織として共有する。

会社が前に出る、とは具体的にどういうことか。たとえば、現場が一人で抱えていた長時間の電話を、責任者が代わって引き取る。SNS上の従業員への攻撃を、会社の名で削除依頼や相談へつなぐ。悪質な事案では、警察・弁護士への相談を会社の判断で動かす。いずれも、個人の力では担えない範囲を、組織が肩代わりするという姿勢の表れだ。現場が「ここから先は会社が引き取ってくれる」と信じられること——それが、毅然とした対応を現場に可能にする土台になる。


# 第7章 反復ワーク

## 7.1 線引きを、言語化する

直近で対応に困った苦情の場面を一つ思い出し、書き出す。

- 要求の中身に、妥当性はあったか
- 伝え方（手段・態様）は、相当な範囲だったか
- 自分は一人で抱えていなかったか。誰に、いつ上げられたか

責めるためではなく、線を確かめるために書く。

## 7.2 一次対応のセリフを、用意する

「お話は承ります。ですが、その言い方／その要求には応じられません」を、自分の言葉に言い換えて書く。毅然と、しかし丁寧に断る一文を、口に出せる形で持っておく。

一人で判定しないための練習として、書いたものを上長・同僚に見せる。


