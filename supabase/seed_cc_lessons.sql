一つ、最初に置いておく。**不調が続くとき、強いとき、生活に支障が出ているときは、自己流のセルフケアで抱え込まず、専門家・産業医・医療機関につなぐ**。本コースは、その手前の自衛と、つなぎ方を渡す。具体の窓口は、第7章「ユーザー追記欄」で自社のものを当てる。


# 第1章 ストレスの仕組み

## 1.1 ストレッサーと、ストレス反応

「ストレス」という一語は、二つの別々のものを指している。混ざると、対処を誤る。

- **ストレッサー**:負荷の原因。仕事量・人間関係・役割の重さ・環境の変化など
- **ストレス反応**:原因を受けて、心・体・行動に出る変化

風で例えるなら、ストレッサーは風そのもの、ストレス反応は、その風を受けて木が揺れる動きだ。同じ風でも、木の状態によって揺れ方は変わる。

- 同じ仕事量でも、感じる負荷は人によって違う
- 同じ人でも、睡眠・体調・時期によって、受け止めは変わる
- だから「これくらいで参るのは甘え」という比較は、成り立たない

ストレッサーをゼロにはできない。仕事がある限り、負荷はかかる。だから戦う場所は、原因の根絶ではなく、**反応に早く気づき、ためこまないこと**に置く。

## 1.2 ストレスは、悪者ではない

適度な負荷は、集中や成長を生む。問題は、負荷の存在ではなく、**強すぎる負荷が、長く続くこと**だ。

- 短く・適度な負荷:回復が追いつく。むしろ力になる
- 強く・長い負荷:回復が追いつかず、すり減っていく

ここで効いてくるのが、回復の時間だ。負荷をかけた分だけ、休養で戻す。この出し入れの収支が崩れ、回復が追いつかなくなったときに、不調はあらわれる。だから本コースは、負荷を消すことより、**回復を確保すること**を重く見る。


# 第2章 不調のサイン ─ 見える不調と、見えない負荷

## 2.1 不調は、水面下から始まる

調子の崩れは、ある日突然落ちるのではない。多くは、水面下で負荷が積もり、ある時に表面化する。

```diagram-iceberg
above: 見えている不調（遅刻・ミス・休みがち・表情）
below: 見えていない負荷 | 眠れない夜 | 抜けない疲れ | 漠然とした不安 | 自分を責める声 | 言い出せなさ
```

周りから見えるのは、水面の上の一部だけだ。遅刻が増えた、ミスが続く、口数が減った——表に出たときには、水面下ではすでに長く負荷が積もっていることが多い。

- 表に出るサインは、氷山の一角にすぎない
- 本人さえ、水面下の負荷に気づいていないことがある
- だから「いつもと違う」を、早い段階で拾う目がいる

これは、自分自身にも、同僚にも当てはまる。早く気づくほど、選べる手は多い。

## 2.2 サインは、心・体・行動の三つに出る

ストレス反応は、一か所に出るとは限らない。三つの領域に分けて見ると、拾いやすい。

```diagram-numbered
title: 不調のサイン（心・体・行動の三領域）
1. 心:気分の落ち込み、イライラ、不安、興味の減退、集中できない
2. 体:眠れない・眠りが浅い、疲れが抜けない、頭痛・肩こり、食欲の変化
3. 行動:遅刻・欠勤の増加、ミスの増加、お酒や買い物が増える、人を避ける
```

注意したいのは、**心の不調が、体や行動に先に出ることがある**点だ。気分の落ち込みより先に、眠れない・食べられない・酒量が増える、という形であらわれる人もいる。「気持ちは元気なつもりだが、体が言うことをきかない」——これも立派なサインだ。

- 一つだけなら、一時的なこともある
- だが、複数が・二週間以上・続くときは、軽く見ない
- とくに「眠れない」が続くのは、見逃せないサイン

ここで線を引いておく。落ち込みや不眠が二週間以上続く、生活や仕事に支障が出ている、消えてしまいたいような考えがよぎる——こうしたときは、セルフケアの範囲を超えている。**専門家・産業医・医療機関につなぐ段階**だと考える。本コースは診断をしない。判断は専門家に委ねる。

## 2.3 同僚のサインは、「いつもと違う」で拾う

他人の心の中は見えない。だが、「いつもと違う」は、外から見える。

- 遅刻・欠勤・早退が増えた
- ミスや物忘れが増えた
- 口数が減った、表情が暗い、イライラしている
- 身だしなみが乱れてきた

これらは、英語圏の職場でも「いつもと違うサイン」として広く共有されている観察点だ。診断ではない。だが、声をかけるきっかけにはなる。気づいた人が、責めずに一声かける——それが、孤立を防ぐ最初の一手になる。


# 第3章 セルフケアの型

セルフケアとは、**自らのストレスに気づき、自分で予防・対処すること**を指す（厚労省「4つのケア」の一つ）。気合いではなく、型で扱う。柱は四つ。

## 3.1 休養と睡眠 ─ 回復の土台

最も基本で、最も削られやすいのが、休養と睡眠だ。

- 睡眠は、心と体を回復させる時間。ここが削れると、すべてが崩れやすい
- 「忙しいから後で寝る」を続けると、回復の収支が赤字になる
- 休日に予定を詰めすぎず、「何もしない時間」を残す

眠れない状態が続くのは、回復が追いついていないサインだ。寝つけない・夜中に目が覚める・朝早く目覚めてしまう、が二週間以上続くなら、自己流で粘らず相談につなぐ。睡眠は、根性で取り戻すものではない。

## 3.2 つながり ─ 一人で抱えない

負荷は、誰かに話すだけでも軽くなる。これは気休めではなく、孤立が負荷を増幅させるからだ。

- しんどさを言葉にすると、頭の中だけで膨らんでいた不安が、少し外に出る
- 「弱音を吐けない」と感じるほど、負荷は内側にこもる
- 信頼できる同僚・家族・友人に、調子を話せる関係を一つ持つ

責任感の強い人ほど、「自分が抱えなければ」と背負い込み、孤立しやすい。だが、抱え込みは美徳ではない。早く声を出すことが、結果として周りも守る。

## 3.3 体を動かす・切り替える

体と心はつながっている。体を整えることは、心の手当てにもなる。

- 軽い運動・散歩は、気分の切り替えに効く
- 仕事から離れる時間（趣味・自然・人と会う）を、意図して確保する
- 「気晴らし」を、わがままではなく回復の手段として扱う

## 3.4 認知の癖を、扱う

同じ出来事でも、受け取り方によって、負荷の大きさは変わる。考え方の癖に気づくと、負荷を少し下げられる。

```diagram-flow
出来事（事実）に気づく
浮かんだ考え（解釈）を、いったん切り離す
「それは事実か、ただの不安の声か」と問い直す
```

考え方の癖を扱う発想は、認知行動の考え方として知られる。難しい理論ではなく、要点は一つ。**頭に浮かんだ考えは、「事実」とは限らない**。

- 「自分は無能だ」「全部自分のせいだ」——これは事実ではなく、解釈
- 浮かんだ考えを、「事実」と「ただの不安の声」に分けてみる
- 自分への厳しい言葉を、親しい人にかける言葉に置き換える（自分を責めすぎない）

これは、無理にポジティブになることではない。事実を歪めず、解釈の暴走だけを少し緩める作業だ。ただし、考え方の工夫で追いつかないほど落ち込みが続くときは、これも専門家の領域になる。セルフケアの型は、不調の手前で効く。深い不調には、専門の手当てがいる。


# 第4章 ストレスチェック制度の趣旨

セルフケアは、個人の努力だけに頼らない。制度の側からも、気づきを支える仕組みがある。その代表が、ストレスチェック制度だ。

- **趣旨**:労働者が自分のストレスの状態に気づき、職場環境の改善につなげる。不調を未然に防ぐための「気づきの仕組み」
- **頻度**:1年以内ごとに1回、定期に実施
- **面接指導**:高ストレスと判定された人は、申し出により医師の面接指導を受けられる
- **対象の拡大**:従来は労働者50人以上の事業場に義務づけられていたが、2025年（令和7年）の法改正により、2028年（令和10年）4月から50人未満の事業場にも実施が義務づけられる（2026年時点）

ここで押さえる原則が二つある。

- **不利益取扱いの禁止**:ストレスチェックを受けたこと・結果・面接の申し出を理由に、解雇・配置転換・降格などの不利益な扱いをしてはならない
- **結果は本人のもの**:個人の結果は、本人の同意なく事業者に提供されない。プライバシーが守られる

ストレスチェックは、点数で人を評価する道具ではない。**自分の状態に気づき、必要なら専門家につながるための入口**だ。「高ストレス」と出たら、それは責められる結果ではなく、手を打つきっかけになる。具体の運用・実施時期・窓口は自社の制度に従う。


# 第5章 専門窓口へのつなぎ方

セルフケアで追いつかないとき、つなぐ先を知っておくと、いざというとき動ける。

- **社内**:産業医、保健師、人事・労務、衛生管理者、社内相談窓口、EAP（従業員支援プログラム）
- **社外・公的**:厚労省「こころの耳」（働く人のメンタルヘルス・ポータルサイト）、各地の産業保健総合支援センター、医療機関（精神科・心療内科）
- **緊急時**:消えてしまいたい気持ちが強いときは、ためらわず医療機関・公的な相談先・身近な人に、その場でつなぐ

つなぎ方の原則は、ハラスメント研修や安全衛生と同じだ。

- 「これくらいで相談していいのか」とためらわない。**迷いの段階で相談していい**
- 早い相談ほど、選べる手は多く、こじれる前に手を打てる
- 受診や相談は、弱さの証明ではなく、調子を立て直すための行動

自社の具体的な窓口・連絡先・EAPの有無は、第7章「ユーザー追記欄」に当てる。窓口が空欄のままだと、いざというときに動けない。連絡先を埋めて初めて、この章は機能する。


# 第6章 管理職のラインケア

> 第6章は、部下を持つ管理職が対象。

ラインによるケア（ラインケア）とは、**管理監督者が、日頃の職場環境を把握・改善し、部下の相談に対応すること**を指す（厚労省「4つのケア」の一つ）。管理職は、部下の「いつもと違う」に最初に気づける位置にいる。

## 6.1 気づく ─ 「いつもと違う」を拾う

- 遅刻・欠勤・ミスの増加、口数の減少、表情の変化——勤怠と様子の変化を見る
- 「最近どう?」と、評価ではなく様子を尋ねる一声を持つ
- 業務量・人間関係・役割の変化など、職場側の負荷要因にも目を向ける

部下の不調は、本人の弱さではなく、職場の負荷の表れであることが多い。個人の問題に閉じず、**職場の構造（業務量・配分・関係性）**に目を向けるのが、ラインケアの核心だ。

## 6.2 聴く ─ 評価を挟まず受け止める

相談を受けたときの初動が、その後を分ける。

- やってはいけないのは、「気にしすぎだ」「みんな大変だ」とその場で結論を出すこと。それは相手を二度傷つける
- まず、評価を挟まず聴く。「話してくれてありがとう」と受け止める
- 解決を急がない。聴くこと自体が、すでに支えになる

管理職は、診断や治療をする立場ではない。深い不調を一人で抱え込もうとせず、**聴いて、つなぐ**ところまでが役割だと割り切る。

## 6.3 つなぐ ─ 一人で抱えない

- 不調が見えたら、産業医・保健師・人事・社内窓口へつなぐ
- プライバシーを守る。本人の同意なく、不調を周囲に広げない
- ストレスチェックや相談を理由に、不利益な扱いをしない

管理職自身も、負荷を最も受けやすい立場にある。部下を支える人ほど、自分のセルフケアと、自分の相談先を持っておく。支える側が倒れないことも、ラインケアのうちだ。


