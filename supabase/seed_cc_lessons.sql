# 第1章 コンプライアンスとは何か

## 1.1 「法令遵守」より、少し広い

コンプライアンスは「法令遵守」と訳されることが多い。だが、それだけでは足りない。

```diagram-layers
foundation: 法令（守らなければ罰せられる最低ライン）
top: 社内規程・契約 | 業界ルール・自主基準 | 社会的要請・倫理（明文化されていない期待）
```

三段で考えると、見通しがよくなる。

- **法令**:破れば罰則がある、絶対の最低ライン
- **社内規程・契約・業界ルール**:組織として守ると決めた約束
- **社会的要請・倫理**:法律にはないが、社会が「それはおかしい」と感じる領域

法律に触れていなくても、社会の信用を失う行為はある。「違法ではない」は、「問題ない」とは違う。コンプライアンスとは、この三段すべてに目を配り、**組織と社会の信頼を守る活動**のことだ。

## 1.2 なぜ「自分ごと」なのか

不正やルール違反は、起こした個人だけの問題では終わらない。

- 顧客・取引先・地域社会の信頼を失う
- 行政処分・損害賠償・取引停止につながる
- 真面目に働いてきた同僚の仕事と誇りを傷つける

一人の小さな違反が、波紋のように広がる。直接かかわった人だけでなく、その商品を使った顧客、取引先、家族、そして組織に残る同僚——あちこちに影響が及ぶ。表に出た一件の裏には、隠れたコストが何倍も積み上がる。

逆に言えば、コンプライアンスは「縛り」ではない。**安心して働ける場と、選ばれ続ける信頼をつくる土台**だ。守りに見えて、実は事業を続けるための攻めの基盤でもある。


# 第2章 なぜ不正は起きるのか ─ 不正のトライアングル

## 2.1 三つが揃ったとき、不正は起きる

不正は、特別に悪い人だけが起こすものではない。米国の研究者ドナルド・クレッシーが整理した「不正のトライアングル」では、不正は次の三つが揃ったときに起きるとされる。

```diagram-triangle
動機（プレッシャー）
機会
正当化
三つすべてが揃ったとき、不正が起きやすくなる
```

- **動機（プレッシャー）**:厳しいノルマ、借金、評価への不安。「やらざるを得ない」と感じる事情
- **機会**:チェックが甘い、一人で完結する、誰も見ていない。「やろうと思えばできる」状況
- **正当化**:「みんなやっている」「会社のためだ」「一時的に借りるだけ」。自分を納得させる理屈

この三角形は、どんな不正を考えるときも出発点になる。新しい事件を見たときも、自分の職場のヒヤリを振り返るときも、まず「三辺のどこに穴があったか」を問えば、論点が定まる。

## 2.2 組織が手を打てるのは「機会」

三辺のうち、組織が最も直接的に減らせるのは**機会**だ。

- 動機（個人のプレッシャー）は、ゼロにはできない
- 正当化（心の理屈）は、本人の内側で起きる
- だが**機会**は、仕組みで減らせる

具体的には、こうした仕組みが「機会」を狭める。

- 一人で完結させない（承認・ダブルチェック・職務の分離）
- 記録を残し、後から確認できるようにする
- 定期的に担当を入れ替える、休暇中に別の人が業務を見る

「人を信じないのか」と感じるかもしれない。だが、これは**人を疑うためではなく、人を不正の機会にさらさないため**の仕組みだ。隙のない仕組みは、結果として真面目な人を守る。

## 2.3 正当化の言葉に、気づく

正当化は、不正の入口を静かに開く。次のような言葉が頭に浮かんだら、立ち止まる合図だ。

- 「これくらい、みんなやっている」
- 「会社のため、お客様のためだ」
- 「バレなければ問題ない」「一回だけ」「あとで戻す」

これらは、行動を正当化するためのものであって、行動が正しいことの証明ではない。**正当化の言葉が出たときほど、第三者に話してみる**。口に出すと、その理屈の弱さが自分でも見えてくる。

正当化が怖いのは、本人が悪いことをしている自覚を持ちにくくなる点だ。「会社のため」と思い込むほど、罪悪感は薄れ、歯止めが効かなくなる。だからこそ、自分の内側の理屈を信じすぎない。判断を、自分の心の中だけで完結させず、外（同僚・上長・窓口）に開く。それが、正当化に流されないための、最も確実な歯止めになる。


# 第3章 「おかしい」と思ったときの動き方

## 3.1 違和感は、初期のサイン

大きな不正も、最初は小さな違和感から始まる。「この処理、いつもと違う」「これは記録に残さないでと言われた」——その引っかかりが、初期のサインだ。

```diagram-flow
違和感に気づく（おかしいと感じる）
一人で抱えず、確かめる・相談する
正式な窓口・通報の仕組みにつなぐ
```

- **気づく**:違和感を「気のせい」で消さない。事実を、その日のうちに短くメモする
- **相談する**:信頼できる上長・同僚、コンプライアンス部門、相談窓口に話す
- **つなぐ**:自分で抱え込まず、正式な窓口や通報の仕組みに乗せる

記録は、特別なものでなくていい。手帳の隅、自分宛のメール。いつ・誰が・何を、を短く残す。記憶は薄れ、上書きされる。だが日付の入った記録は残る。これは誰かを陥れるためではなく、自分の見たことを正確に説明できるようにするための備えだ。

## 3.2 「これくらいで相談していいのか」への答え

相談をためらう人は多い。「大ごとにしたくない」「自分の勘違いかもしれない」——その迷いは自然だ。だが、相談窓口は白黒をつける裁判所ではない。**迷いの段階で話していい場所**だ。

- 早い相談ほど、こじれる前に手を打てる
- 確証がなくても、違和感の段階で相談していい
- 相談したことで不利益に扱われない仕組みが、法律で求められている（第4章）

一人で「これは不正だ」と断定する必要はない。判断は、組織の仕組み（窓口・規程・専門家）に乗せる。あなたの役割は、気づき、つなぐことだ。


# 第4章 通報の仕組み ─ 公益通報者保護法

## 4.1 法律が、通報した人を守る

組織の不正を知った人が声を上げられるよう、日本には「公益通報者保護法」がある。一定の要件を満たして通報した人を、**通報を理由とした不利益な取扱いから守る**ための法律だ（2026年時点。詳細は消費者庁の解説と自社規程を確認すること）。

主要な見解では、保護の中身はおおむね次のように整理される。

- 通報を理由とした**解雇は無効**になる
- 降格・減給・退職金不支給などの**不利益な取扱いが禁止**される
- 通報にともなう**損害賠償責任が免除**される場合がある

つまり、「告げ口した人が損をする」構造をなくし、正直に声を上げた人を守るのが、この法律の趣旨だ。

## 4.2 三つの通報先と、保護の要件

公益通報には、大きく三つの通報先がある。通報先によって、保護されるための要件が変わる（一般的な整理）。

```diagram-numbered
title: 公益通報の三つの窓口
1. 事業者内部（社内の通報窓口）── 要件が最もゆるやか
2. 行政機関（権限を持つ役所）── 真実相当性などが必要
3. 報道機関など外部 ── さらに追加の要件が必要
```

- **まずは社内の窓口へ**:最も要件がゆるく、組織が自ら是正できる機会にもなる
- 社内で対応されない、証拠隠滅のおそれがある等の場合に、行政機関・外部という選択肢が出てくる
- どの窓口でも、通報者の**氏名などの秘密は守られる**べきものとされる

具体の要件は法律の条文と運用で細かく定められている。「自分のケースはどの窓口か」を一人で判断せず、自社の通報窓口・コンプライアンス部門・弁護士に確認する。

## 4.3 事業者には、体制を整える義務がある

2020年改正（2022年6月施行）により、一定規模以上の事業者には、内部通報に対応する体制の整備が求められるようになった（常時使用する労働者が一定数を超える事業者は義務、それ以下は努力義務とされる）。

- 通報を受け付ける**窓口の設置**
- 通報を扱う担当者の指定と、その**守秘義務**
- 通報者が**不利益に扱われないようにする**措置

自社の窓口がどこにあり、誰が担当か——それは第7章「ユーザー追記欄」で必ず当てる。窓口の連絡先を知らなければ、いざというとき使えない。


# 第5章 身近なテーマの最低限

法律や制度は幅広いが、全社員が最低限おさえたい身近なテーマを、四つに絞る。いずれも「自分は関係ない」と思った瞬間に、足をすくわれやすい領域だ。具体の線引きは、必ず自社規程と専門家に確認する。

## 5.1 利益相反

利益相反とは、**会社の利益と、自分（や身内）の利益がぶつかる立場**に立つことだ。

- 取引先の選定に、個人的な利害がからむ
- 会社の情報や立場を、私的な利益のために使う
- 副業・兼業が、会社の業務と競合する

利益相反そのものが直ちに悪いわけではない。問題は、**隠すこと**だ。利害がぶつかりうる立場に立ったら、隠さず上長や所定の部門に申告する。透明にすれば、多くは適切に管理できる。担当を外れる、第三者の確認を入れるなど、対応の選択肢は申告して初めて開かれる。隠したまま進めれば、後で「私的に利益を図った」と見られかねない。

## 5.2 贈収賄・接待

公務員への金品の提供は、贈賄として処罰されうる。海外の公務員への贈賄も、不正競争防止法で禁止され、重い罰則が定められている（経済産業省「外国公務員贈賄防止指針」。2026年時点）。

- 公務員・みなし公務員への金品・過剰な接待は、原則として避ける
- 民間どうしでも、過剰な接待・贈答は不正の温床になる
- 「相手から求められた」「慣習だ」は、正当化の言葉になりやすい

迷ったときの目安は、「**第三者に堂々と説明できるか**」だ。記録に残せない、人に言えない金品のやりとりは、一度立ち止まる。具体の基準（金額・申告手続き）は自社規程に従う。

## 5.3 下請法（取適法）

立場の強い発注者が、弱い受注者に不当な扱いをすることを防ぐ法律がある。下請法は、2026年1月の改正で名称・規制内容が見直され、「取適法」とも呼ばれる（公正取引委員会。最新の名称・内容は一次情報を確認すること）。

代表的な禁止行為の例。

- 正当な理由のない**支払いの遅延・代金の減額**
- 受注者に責任がないのに**受領を拒否・返品**する
- 不当に**経済上の利益の提供を要請**する

注意したいのは、**受注者の了解を得ていても、悪意がなくても違反になりうる**点だ。発注する側に立つことがあるなら、自社の調達・法務のルールを必ず確認する。

## 5.4 情報の適正利用

仕事で得た情報は、私物ではない。

- 顧客・取引先の**個人情報・機密情報**を、目的外に使わない・漏らさない
- 会社の**未公開情報**を、私的な取引（インサイダー取引等）に使わない
- SNSや会話で、業務上知った情報を不用意に話さない

情報の取り扱いは別コース（CC-02 情報セキュリティ・個人情報保護）で詳述する。ここでは「**預かった情報を、目的の外で使わない**」という原則だけ、押さえておく。


# 第6章 ありがちな失敗

熟練した人でも通る道。気づいて直せればいい。大切なのは、自分は大丈夫だと思い込まないことだ。

**失敗① 「会社のため」で、線を越える**

目的が正しくても、手段が不正なら線を越える。「数字のため」「お客様のため」という大義は、手段の暴走を覆い隠しやすい。目的の正しさと、手段の適正さは、別々に点検する。

**失敗② 「みんなやっている」で、流される**

慣習や周囲の空気は、強い正当化になる。だが「みんな」がやっていることが、正しいとは限らない。判断の基準は、周囲ではなく、法令・規程・「第三者に説明できるか」に置く。

**失敗③ 違和感を、見て見ぬふりする**

「自分には関係ない」「波風を立てたくない」——その沈黙が、不正を長引かせる。直接止めるのが難しくても、記録する、窓口へつなぐ、という間接的な行動はいくつもある。沈黙だけが、何もしない選択だ。

**失敗④ 一人で抱え込み、一人で判断する**

「自分が騒げば立場が悪くなる」「勘違いかもしれない」——その思い込みが、声を止める。だからこそ、迷いの段階で相談する。一人で「不正だ／不正でない」を判定しない。それが、踏み越えと見逃しの、両方を防ぐ。

**失敗⑤ 「違法でなければいい」で済ませる**

法律の網の目をくぐっても、社会の信頼は失われる。コンプライアンスは法令の一段上に、社会的要請・倫理の層がある（第1章）。「違法ではない」で立ち止まらず、「説明できるか」まで問う。


# 第7章 反復ワーク

## 7.1 不正のトライアングルで、職場を見る

自分の業務で、「機会」が生まれやすい場面を一つ書き出す。

- 一人で完結し、誰のチェックも入らない作業はないか
- 記録が残らない、後から確認できない処理はないか
- その「機会」を狭めるには、どんな一手があるか

責めるためではなく、仕組みの穴を見つけるために書く。

## 7.2 正当化の言葉を、言語化する

過去に「これくらいなら」と感じた場面を一つ思い出し、そのとき頭に浮かんだ正当化の言葉を書く。書いて、それが「正しさの証明」ではなく「自分を納得させる理屈」だったかを確かめる。一人で判定しないための練習だ。


