- 書類の入った鞄を電車に置き忘れる
- 届いた添付ファイルを、疑わずに開く

どれも、専門知識とは関係がない。情報を扱うすべての人が、その日のうちにやってしまえる、ごく普通の動作だ。だからこそ、情報セキュリティは情報システム部門だけの問題ではなく、**情報に触れるすべての人の問題**になる。

この研修の目的は、あなたを技術者にすることではない。**預かった情報を漏らさないための、最低限の作法と勘どころを渡すこと**だ。顧客の名前、取引先の連絡先、同僚の個人情報——あなたの手元を通る情報は、誰かがあなたを信じて預けたものだ。

完璧な防御はない。穴はゼロにできない。本コースの目的は、リスクを過度に恐れて手が止まることでも、無防備に流すことでもなく、**危ない場面で一拍立ち止まれる目を持つこと**だ。

本コースは2026年時点の普遍的な原理に絞る。法令の細かい数値・条番号、自社固有のルールは薄い。具体の判断は、自社規程・情報システム部門・個人情報保護委員会・弁護士等の専門家に従う。本コースは、その手前の共通言語を渡す。


# 第1章 個人情報とは何か

## 1.1 「個人情報」の輪郭

まず、守る対象の輪郭を押さえる。個人情報とは、ざっくり言えば、**生きている個人を特定できる情報**だ。

- 氏名、生年月日、住所、顔写真
- 単体では特定できなくても、他の情報と照らせば特定できるもの
- 個人を識別できる番号・符号（マイナンバー、運転免許証番号など）

「名前さえ伏せれば大丈夫」と考えがちだが、そう単純ではない。所属・役職・経歴を組み合わせれば、名前がなくても個人にたどり着くことがある。**単体ではなく、組み合わせで特定できるか**を見るのが要点になる。

たとえば「ある部署の、入社三年目の、地方支店から異動してきた人」と書けば、名前を一切出さなくても、社内では一人に絞り込めてしまう。匿名のつもりが、文脈の中では実名と変わらない。情報を外に出すときは、名前を消したかどうかではなく、**読んだ人が個人を言い当てられるかどうか**で線を引く。これは、SNSの投稿でも、社内のチャットでも、外部への報告書でも、同じように働く目だ。

## 1.2 とりわけ慎重に扱う「要配慮個人情報」

個人情報の中でも、漏れると本人が差別や偏見にさらされやすい情報は、**要配慮個人情報**として、より慎重な扱いが求められる。

- 人種、信条、社会的身分
- 病歴、心身の機能の障害、健康診断の結果
- 犯罪の経歴、犯罪により害を被った事実

これらは、原則として取得の段階で本人の同意がいる、と考えておく。「うっかり集めてしまった」「雑談で聞いた」では済みにくい領域だ。健康・病歴・経歴にかかわる話に触れるときは、一段ギアを上げて慎重になる。

個人情報は、データベースの中だけにあるのではない。受付の来訪者名簿、机の上の名刺、チャットに貼られた顧客名——日常のあらゆる場所に散らばっている。「これは個人情報か」と一度問う癖が、守りの起点になる。


# 第2章 取り扱いの基本 ─ 取得から廃棄まで

## 2.1 情報には「一生」がある

個人情報は、手に入れて終わりではない。取得され、使われ、保管され、やがて廃棄される。この一連の流れの各段に、それぞれの注意がある。

```diagram-layers
foundation: すべての段で共通：必要最小限・目的の範囲・アクセスの制限
top: 取得（同意・目的の明示） | 利用（目的の範囲内） | 保管（守る・分ける） | 廃棄（戻せない形で消す）
```

下のベース（必要最小限・目的の範囲・アクセス制限）は、どの段でも変わらない。上に乗る注意点だけが、段ごとに変わる。情報の一生を四つの段で捉えると、自分が今どの段の作業をしているかが見え、抜けやすい注意が浮かぶ。

## 2.2 取得 ─ 何のために集めるか

- 利用目的を、あらかじめはっきりさせる
- いるものだけを集める（「念のため」で余計に集めない）
- 要配慮個人情報は、原則として本人の同意を得る

集めた情報は、集めた瞬間から守る責任が発生する。**集めない情報は、漏れない**。最大の防御は、不要な情報を最初から持たないことだ。「あとで使うかもしれない」で抱え込んだ情報ほど、管理から漏れ、忘れたころに事故になる。手元に置く情報は、少ないほど守りやすい。

## 2.3 利用 ─ 目的の範囲を超えない

- 集めたときに伝えた目的の範囲内で使う
- 範囲を超えて使うなら、原則として本人に改めて知らせ、同意を得る
- 「業務に便利だから」で、当初の目的外に流用しない

## 2.4 保管 ─ 守る・分ける・絞る

- 鍵のかかる場所・アクセス制限のかかる領域に置く
- 見られる人を、必要な人だけに絞る
- 持ち出し・複製のルールを、自社規程に従って守る

## 2.5 廃棄 ─ 戻せない形で消す

- 紙はシュレッダーや溶解、データは復元できない形で消去する
- 「ゴミ箱に捨てる」「ファイルを削除する」だけでは、復元される恐れがある
- 不要になった情報は、ためずに、適切に消す

各段の具体的なやり方（保管場所・保存期間・廃棄方法）は、自社規程と情報システム部門の指示に従う。本コースは、四つの段それぞれに固有の注意があるという「地図」を渡す。


# 第3章 改正個人情報保護法の要点

個人情報保護法は、たびたび改正されている。細かい条文は専門家に委ね、ここでは**現場で押さえる考え方**を四つに絞る（2026年時点）。

- **利用目的の明示**：何のために使うかを、本人に分かる形で示す
- **本人の同意**：要配慮個人情報の取得や、目的外の利用には、原則として本人の同意がいる
- **第三者提供のルール**：本人の同意なく、第三者に個人データを渡さないのが原則。委託・共同利用など例外の枠組みはあるが、勝手な判断で外に出さない
- **漏えい等の報告・通知義務**：一定の漏えい等が起きたとき、個人情報保護委員会への報告と、本人への通知が義務づけられている

特に四つ目は、近年の改正で強化された点だ。「漏らしても黙っていればよい」は、もはや通用しない。**漏れたら、報告し、本人に知らせる**——その義務がある前提で動く。

ただし、報告・通知が必要になる範囲や、報告の期限・様式には、細かい要件がある。「これは報告対象か」を一人で判断しない。漏えいの疑いがあれば、まず自社の窓口・情報システム部門・個人情報保護委員会の案内に従う。法令の具体的な解釈・対応は、自社規程と専門家の指示が優先する。


# 第4章 インシデント類型 ─ 漏れ方には型がある

情報漏えいは、無限の形で起きるわけではない。**起きやすい型**が決まっている。型を知れば、どこで気をつけるかが見える。

```diagram-numbered
title: 起きやすいインシデントの型
1. メール誤送信（宛先間違い・添付ミス・一斉送信のCC/BCC取り違え）
2. 書類・USB・端末の紛失（置き忘れ・盗難）
3. 標的型メール（業務を装った添付・リンクでウイルス感染）
4. なりすまし・ビジネスメール詐欺（取引先や上司を装い、送金・情報を要求）
```

## 4.1 メール誤送信

最も日常的で、最も起きやすい。宛先の一文字違い、添付ファイルの取り違え、一斉送信でのCCとBCCの取り違え（他人のメールアドレスが全員に見える）。

- 送信前に、宛先・添付・本文を一拍おいて確認する
- 社外への一斉送信は、原則BCC。CCとの違いを体で覚える
- 重要な送信は、ダブルチェックや送信保留の仕組みを使う

## 4.2 書類・USB・端末の紛失

紙の資料、USBメモリ、ノートパソコン、スマートフォン。持ち出した瞬間、紛失・盗難のリスクが立ち上がる。

- 持ち出しは、本当に必要なときだけ。ルールに従う
- 端末・USBは暗号化し、放置しない
- 公共の場（電車・カフェ・タクシー）に置き忘れない

## 4.3 標的型メール

実在の取引先や社内の部署を装い、ウイルス付きの添付やリンクを開かせる。IPAの「情報セキュリティ10大脅威」でも、標的型攻撃やランサム攻撃は組織の主要な脅威として毎年挙がっている（2026年版）。

- 心当たりのない添付・リンクは、開く前に疑う
- 「至急」「重要」で焦らせる文面ほど、一度立ち止まる
- 不審なら、開かず情報システム部門へ確認する

## 4.4 なりすまし・ビジネスメール詐欺

取引先や経営層を装い、振込先の変更や送金、情報の送付を求める。文面が巧妙で、本物そっくりに見える。

- 振込先変更・送金の依頼は、メールだけで完結させない
- 電話など別の手段で、本人に直接確かめる
- 「急いで」「内密に」は、危険信号として扱う


# 第5章 パスワードと端末の管理

技術的な守りの中で、誰もが毎日関わるのが、パスワードと端末だ。ここは専門家でなくても、自分の手で守れる領域になる。

## 5.1 パスワードの基本

- 長く、推測されにくいものにする（誕生日・名前・連番は避ける）
- 使い回さない（一つ漏れると、芋づる式に破られる）
- 付箋でモニターに貼らない。管理は自社で認められた方法に従う
- 可能なら、多要素認証（パスワード＋ワンタイムコード等）を使う

パスワードは、家の鍵と同じだ。**合鍵を配り歩かない、玄関に貼らない**——その当たり前を、デジタルでも守る。

## 5.2 端末の管理

- 離席するときは、画面をロックする（短い離席でも）
- 業務端末に、許可されていないソフトを勝手に入れない
- ソフトの更新（アップデート）を後回しにしない。穴をふさぐのが更新だ
- 公共のWi-Fiで、機密情報を扱わない

端末は、情報への入口だ。入口が開けっ放しなら、どれだけ中を守っても意味がない。**席を立つ前に、画面をロックする**。この一動作が、最も安く効く防御になる。更新（アップデート）の通知を「あとで」と閉じ続けるのも、入口を開けたままにする行為だ。更新は、見つかった穴をふさぐ作業であり、後回しにした分だけ、無防備な時間が延びる。面倒に見えて、これも立派なセキュリティ対策になる。


# 第6章 SNS・私物端末（BYOD）の線引き

仕事と私生活の境目で、情報は静かに漏れる。悪意がなくても、線を越えれば漏えいになる。

## 6.1 SNS ─ 何を出してよいか

- 業務で知った情報（顧客・取引先・社内の機微）を、私的なSNSに書かない
- 「鍵アカウントだから」「身内しか見ていない」は、安全の保証にならない
- 写真の背景に、書類・画面・名札が写り込んでいないか確かめる

楽しい投稿、よかれと思った発信が、一枚の写真の隅から情報を漏らすことがある。**出す前に、背景と中身をもう一度見る**。

## 6.2 私物端末（BYOD）の線引き

私物のスマートフォンやパソコンで仕事をすること（BYOD）には、便利さと引き換えのリスクがある。

- 業務情報を私物端末に入れてよいかは、自社規程による。勝手に判断しない
- 認められている場合も、私的な利用と業務の情報を混ぜない
- 端末を手放す・廃棄するときは、業務情報の消去を忘れない

私物だからこそ、管理が緩む。家族と共用していたり、無料アプリを無造作に入れていたり——その緩さが、業務情報の漏れ口になる。線引きは、必ず自社規程に従う。


# 第7章 漏えい時の初動 ─ 止める・報告・拡大防止

どれだけ気をつけても、漏えいは起こりうる。そのとき何より大切なのは、**慌てて隠さないこと**だ。初動の良し悪しが、被害の大きさを決める。

```diagram-flow
止める（送信取り消し・端末切り離し・拡散停止）
報告する（上長・情報システム部門・自社の窓口へ即座に）
拡大防止と通知（事実確認・原因究明・本人や委員会への対応）
```

## 7.1 止める

- 誤送信なら、可能なら送信を取り消し、受信者に削除を依頼する
- ウイルス感染の疑いなら、端末をネットワークから切り離す
- これ以上広がらないよう、まず流れを止める

## 7.2 報告する

- 隠さず、すぐに上長・情報システム部門・自社の窓口へ報告する
- 「自分のミスだから」と抱え込むほど、被害は深く、静かに広がる
- 報告は犯人探しではなく、被害を最小にするための第一歩だ

## 7.3 拡大防止と通知

- 何が・どれだけ漏れたかを確認し、原因を究明する
- 必要に応じて、本人への通知や個人情報保護委員会への報告を行う（第3章）
- 具体の手順・判断は、自社規程と専門家の指示に従う

初動で最もやってはいけないのは、「大ごとにしたくない」と黙ってしまうことだ。報告が遅れるほど、対応の選択肢は減り、信頼の傷は深くなる。**漏らしたことより、隠したことのほうが、組織を壊す**。迷ったら、まず報告する。


# 第8章 反復ワーク

## 8.1 自分の手元を、棚卸しする

直近の業務を一つ思い出し、書き出す。

- その作業で、どんな個人情報・機密情報に触れたか
- それは、取得・利用・保管・廃棄のどの段にあったか
- 守りが甘かった点は、どこか（宛先確認・施錠・画面ロック等）

責めるためではなく、穴を見つけるために書く。

## 8.2 「危ない場面」を、言語化する

「これは漏らしかけた」「ヒヤッとした」場面を一つ挙げ、なぜ危なかったかを書く。そして、次に同じ場面が来たら何を一拍おくかを、一行で決める。一人で抱えず、同僚や上長と共有すれば、職場全体の備えになる。


