- 違いを「面倒」と扱えば、コストになる
- 違いを「戦力」に変えられれば、組織の強みになる
- その分岐点に、無意識バイアスがある

本コースの目的は、特定の価値観を押しつけることではない。自分の中にある無意識の思い込みに気づき、それが採用・評価・配置・日々の一言にどう影を落とすかを見えるようにすることだ。バイアスは「悪人の偏見」ではなく、脳が効率のために持つ正常な機能だ。だから消せない。消せないものを、自覚して扱う——それが出発点になる。

完璧な公平はない。本コースの目的は、白黒をつけることではなく、自分の判断に潜む偏りに立ち止まれる目を持つことだ。

具体の判断は、自社の人事・就業規則・産業医・弁護士・専門機関に従う。本コースは、その手前の共通言語を渡す。


# 第1章 D&Iとは何か

## 1.1 多様性（D）と包摂（I）は、別の話

D&Iは二つの言葉でできている。順に押さえる。

- **ダイバーシティ（多様性）**:性別・年齢・国籍・障害・育児/介護の事情・価値観など、構成員に違いがある状態
- **インクルージョン（包摂）**:その違いを持つ一人ひとりが、力を発揮でき、意思決定に参加できている状態

多様性だけでは足りない。違う人を集めても、声が届かず、力を出せなければ、多様性はただの数字で終わる。

よく使われるたとえがある。**ダイバーシティはパーティーに招かれること、インクルージョンはそのダンスに誘われること**。招かれただけで踊れない人がいる職場は、多様だが包摂されていない。

## 1.2 「包摂」がなければ、多様性は逆効果になりうる

包摂を欠いた多様性は、かえって摩擦を生むことがある。

- 違う背景の人を集めただけで、相互理解の仕組みがない
- 少数派が「お客様扱い」され、本音の意見を言えない
- 結果、対立や離職が増え、「多様性は面倒」という誤った学習が残る

だからD&Iは、**多様性を入り口、包摂を本体**として設計する。違いを集めること自体が目的ではなく、違いを戦力に変える状態をつくることが目的だ。

```diagram-layers
foundation: 多様性（違いのある人材が集まっている）
top: 包摂（一人ひとりが力を発揮し参加できる）→ 成果（意思決定の質・人材の定着・革新）
```

この三層は、上から崩れる。包摂が欠ければ成果は出ず、成果が出なければ「多様性は無駄だった」と入り口まで否定される。だからこそ、真ん中の包摂を、管理職が支える。


# 第2章 なぜ経営課題なのか

D&Iは、倫理や流行の話に見えて、実は経営の数字に直結する。理由を三つに絞る。

## 2.1 人材の確保

労働人口が縮む中で、特定の属性だけに頼る採用は、人材プールを自ら狭める。

- 多様な人材に開かれていない職場は、採用の母集団が小さい
- 育児・介護・障害などの事情を理由に人を失えば、採用・育成のコストが繰り返しかかる
- 「ここでは力を出せない」と感じた人は、静かに辞めていく

違いを受け入れられる職場は、人を集めやすく、長くとどめやすい。これは福祉ではなく、人材戦略の問題だ。

## 2.2 意思決定の質

同じような人だけで決めると、判断は速いが、死角ができる。

- 似た者同士は、似た前提を共有し、同じ穴を見落とす（集団浅慮）
- 異なる視点が入ると、前提が問い直され、リスクや機会に気づきやすい
- 顧客が多様なら、つくる側も多様なほうが、ニーズを捉えやすい

違いは、衝突の種であると同時に、より良い判断の材料でもある。摩擦を恐れて同質に閉じると、判断の質まで失う。

## 2.3 変化への強さ

前提が揺らぐ時代には、一つのものの見方に頼る組織はもろい。多様な視点を内部に持つことは、環境変化への備えになる。一般に、多様性と包摂が両立した組織は、革新や問題解決の面で優位を持ちやすいとされる。

ただし、効果は自動では出ない。多様性を成果に変えるのは、包摂の運用と、次章で扱う**無意識バイアスへの自覚**だ。


# 第3章 無意識バイアスの典型

## 3.1 バイアスは「正常な機能」である

無意識バイアス（アンコンシャス・バイアス）とは、自分では気づかないうちに持つ、偏った見方や思い込みを指す。

- 脳は、大量の情報を高速で処理するために、パターンで判断する
- パターン化は便利だが、特定の属性への決めつけを生む
- 本人に悪気はなく、むしろ「自分は公平だ」と思っている人ほど気づきにくい

内閣府男女共同参画局の調査研究（令和3年度）では、性別による無意識の思い込みが広く存在することが示されている。たとえば「男性は仕事をして家計を支えるべき」「女性には家庭的な役割を期待する」といった思い込みが、多くの回答者に見られたと報告されている（2026年時点。最新の数値は一次情報を確認）。

重要なのは、これが**例外的な偏見ではなく、誰の中にもある**という点だ。だから「自分は差別しない」では対策にならない。「自分にもバイアスがある」を前提に、仕組みで補う。

## 3.2 職場で効く四つの型

```diagram-numbered
title: 職場で効く無意識バイアスの型
1. 類似性バイアス（自分と似た人を高く評価する）
2. 確証バイアス（最初の印象を裏づける情報だけ集める）
3. 正常性バイアス（「うちは大丈夫」と問題を過小評価する）
4. ステレオタイプ（属性で個人を決めつける）
```

順に、職場の場面に置く。

- **類似性バイアス**:出身校・経歴・趣味が自分に近い部下を、無意識にひいきする。採用面接で「一緒に働きやすそう」が、能力評価にすり替わる
- **確証バイアス**:「あの人は詰めが甘い」と一度思うと、ミスばかり目につき、成功は「たまたま」と片づける。評価が最初の印象に固定される
- **正常性バイアス**:「うちにハラスメントや差別はない」と思い込み、小さな兆候を見過ごす。問題は「ない」のではなく「見えていない」だけのことがある
- **ステレオタイプ**:「子どもがいるから残業は無理だろう」「若いから責任ある仕事はまだ」と、本人に確かめず属性で決める

四つに共通するのは、**個人を、個人として見ていない**という構造だ。目の前の一人ではなく、頭の中のパターンを見ている。


# 第4章 マイクロアグレッション ─ 悪気なき小さな否定

## 4.1 氷山の下にあるもの

差別は、目に見える露骨な言動だけではない。日常に埋もれた、小さな否定の積み重ねがある。これを**マイクロアグレッション**（悪気なき小さな否定）と呼ぶ。

```diagram-iceberg
above: 意識された差別（露骨な侮辱・排除・不当な扱い）
below: 無意識の思い込み | 悪気なき一言（マイクロアグレッション）| 「普通は」という前提 | 見えていない決めつけ
```

水面の上の露骨な差別は、誰の目にもわかり、糾弾もされる。だが職場で日々起きているのは、水面下の、本人も気づかない小さな否定のほうだ。一つひとつは軽く見え、言った側は「褒めたつもり」「親しみのつもり」でいる。

## 4.2 「褒めたつもり」が、否定になる

具体の例を挙げる。

- 「女性なのに数字に強いね」——性別への思い込みを前提にした「褒め言葉」
- 「日本語、上手ですね」——相手を最初から「外」の人と位置づける
- 「お子さんがいるのに、よく頑張るね」——育児と仕事の両立を「例外」扱いする
- 「もう若くないんだから無理しないで」——年齢で能力に線を引く

共通するのは、**相手を一つの属性で代表させ、「普通ではない」と暗に伝えている**点だ。一回なら受け流せても、毎日浴びれば、相手は「自分はこの場では異質な存在だ」と学習し、力を出しにくくなる。

ハラスメントと同じく、判断の重心は**受けた側の受け止め**にある。「そんなつもりはなかった」は出発点にはなるが、否定をなかったことにはしない。気づいて、次から言い換える——それで足りる。


# 第5章 合理的配慮 ─ 違いを「戦力化」する実務

## 5.1 合理的配慮とは何か

違いを戦力に変えるには、違いに応じた環境調整がいる。これを**合理的配慮**と呼ぶ。

- 障害のある人が、障害のない人と同じように働けるよう、職場の側が調整を行うこと
- 改正障害者雇用促進法では、雇用のあらゆる局面での障害者差別が禁止され、合理的配慮の提供が事業主の義務とされている（厚生労働省。平成28年4月施行）
- 対象は障害者手帳の有無に限られず、身体・知的・精神（発達障害を含む）など、心身の機能に支障がある人が広く含まれるとされる

配慮の例（あくまで一般的な例。具体は本人との相談と専門機関の助言による）。

- 視覚に障害のある人へ、画面読み上げソフトや書類の電子化
- 聴覚に障害のある人へ、筆談・チャット・字幕の活用
- 精神・発達障害のある人へ、指示の文書化、静かな作業環境、通院への配慮

## 5.2 「過重な負担」と、対話で決める原則

合理的配慮には、二つの原則がある。

- **過重な負担にならない範囲**:事業の規模や費用に照らして過大な負担となる場合は、その限りでない。ただし「できない」で終わらせず、代替策を本人と探る
- **本人との対話で決める**:何が必要かは人によって違う。良かれと推測で決めず、本人の希望を聞いて、できることを一緒に決める

合理的配慮は、障害に限った話ではない。考え方は、育児・介護との両立にも通じる。

- 育児・介護を抱える人へ、時短・時差・在宅などの柔軟な働き方
- 制度を「使う人を特別扱い」とせず、使える前提で職場を回す
- 配慮を「お情け」ではなく、その人の力を引き出す**投資**として扱う

合理的配慮の本質は、特別優遇ではない。**スタートラインを揃え、持っている力を出せるようにする**ことだ。配慮は、違いを戦力に変える具体的な手段になる。


# 第6章 管理職の振る舞い ─ 公平な機会と心理的安全性

> 本章は、部下を持つ管理職（G2〜G4）が主対象。

## 6.1 バイアスは「仕組み」で補う

管理職は、採用・評価・配置という、人の機会を左右する判断を日々下す。だからこそ、自分のバイアスの影響が最も大きい立場でもある。意志の力だけで偏りを消すのは難しい。仕組みで補う。

- **評価基準を先に決める**:人を見てから基準を作ると、印象に基準が引っ張られる。基準は対象を見る前に言語化する
- **事実で語る**:「なんとなく優秀」ではなく、行動・成果の事実で評価する。印象語を事実に翻訳する習慣を持つ
- **機会を均す**:挑戦の機会・重要な仕事・情報が、特定の人に偏っていないか点検する。「あの人には無理だろう」で機会を奪っていないか
- **一人で決めない**:採用・評価は複数の目を通す。バイアスは他者の視点で見えやすくなる

## 6.2 心理的安全性と接続する

D&Iは、心理的安全性と表裏一体だ。違いがあっても安心して意見を言える場がなければ、包摂は成立しない。

```diagram-layers
foundation: 心理的安全性（違っても、安心して声を出せる）
top: 包摂（少数派の意見も意思決定に届く）→ 多様性が成果に変わる
```

- 少数意見を、その場で否定せず、まず受け止める
- 「みんな同じ」を前提にした冗談や決めつけを、管理職が率先してやめる
- 配慮を求めること・違いを口にすることが、不利益にならないと示す

管理職自身が、職場で最も強い影響力を持つ。その一言が、場の空気の基準になる。だからこそ、自分の言動に最初に線を引く立場でもある。公平な機会と安心して話せる場——この二つを管理職が支えたとき、多様性は初めて成果に変わる。


