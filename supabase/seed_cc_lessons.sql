BCP（事業継続計画）とは、平常時のうちに、その「止まったあとの戻り方」を決めておく備えだ。中小企業庁は、緊急事態に遭っても、損害を最小限にとどめ、中核となる事業の継続あるいは早期復旧を可能にするために、平常時に取り決めておく計画、と定義している（2026年時点）。

本コースは、管理職（G3〜G4）が自部門の事業継続を考えるための、共通の物差しを渡す。BCPそのものを完成させる本ではない。完成は、自社のBCP・責任者・専門家とともに行うものだ。本コースは、その手前で「何を考えるべきか」の地図を渡す。

完璧な計画はない。すべての災害を防ぐこともできない。本コースの目的は、止まらないことではなく、**止まっても戻れる組織の考え方**を、あなたの中に置くことだ。


# 第1章 BCPとは何か ─ 防災との違い

## 1.1 防災は「守る」、BCPは「戻す」

防災とBCPは、近いようで目的が違う。

- **防災**:人命と財産を、被害から守る。被害そのものを小さくすることが目標
- **BCP**:被害を受けた前提で、事業を止めない／早く戻す。組織が生き延びることが目標

内閣府は、防災が「災害被害の軽減」を目標とするのに対し、BCPは「企業そのものが生き延びること」を求める経営戦略だ、と位置づけている（2026年時点）。

```diagram-layers
foundation: 平時（備える）─ 重要業務の特定・代替手段の用意・連絡網・訓練
top: 発災（止める／守る）─ 人命安全・安否確認・初動 | 復旧（戻す）─ 重要業務から順に再開
```

二段で考える。土台は平時の備え。その上に、発災時の初動と、復旧の動きが乗る。土台がなければ、発災時にいくら頑張っても、戻る先がない。

## 1.2 「止めない」と「早く戻す」は別の手段

事業継続には、二つの戦略がある。混同しない。

- **止めない（継続）**:重要業務が中断しないよう、二重化・分散で守る
- **早く戻す（復旧）**:いったん止まることを認め、許される時間内に再開する

すべてを「止めない」で守るのは、費用がかかりすぎて現実的でない。多くの業務は「早く戻す」で設計する。どこを「止めない」で守り、どこを「早く戻す」で許すか——その線引きが、BCPの設計の中心になる。

## 1.3 BCPは「経営の判断」であって、防災担当の仕事ではない

BCPを、設備や備蓄の話だと誤解しやすい。

- 何を最優先で守るかは、事業の優先順位の判断だ
- 限られた人・物・金を、どこに割り振るかの判断だ
- だから、現場の担当ではなく、**部門を預かる管理職・経営層が決める**べきことになる

あなたが管理職なら、自部門のBCPの当事者だ。具体の作り込みは専門家と進めるとしても、「何を最優先に戻すか」の判断は、現場を知るあなたにしか下せない。


# 第2章 重要業務の特定と優先順位

## 2.1 全部は守れない、を出発点にする

発災後、使える人・物・金・情報は限られる。

- すべての業務を、同時に、平常どおり続けることはできない
- 何かを後回しにしなければ、結局すべてが中途半端に倒れる
- だから、**何を最優先で復旧するか**を、平時に決めておく

内閣府は、優先的に継続または早期復旧を必要とする「重要業務」を慎重に選ぶこと、と求めている（2026年時点）。BCPの第一歩は、増やすことではなく、**絞ること**だ。

## 2.2 重要業務を選ぶ問い

どの業務を「重要」とするか。次の問いで絞る。

- 止まると、顧客・取引先・社会への影響が最も大きいのはどれか
- 止まると、収益・信用の損失が最も大きいのはどれか
- 法令・契約上、止めてはならないものはどれか
- 後回しにしても、当面は致命傷にならないのはどれか

「重要でない業務」を決めることも、同じくらい大切だ。後回しにしてよいものを先に外せば、守るべきものが浮かび上がる。

## 2.3 目標復旧時間（RTO）─ 「いつまでに」を数字にする

重要業務を選んだら、それぞれに「いつまでに戻すか」を決める。

- **目標復旧時間（RTO）**:その業務を、何時間／何日以内に再開させるか
- **目標復旧レベル**:どの水準まで戻すか（平常の何割か）

内閣府は、各重要業務に目標復旧時間（Recovery Time Objective, RTO）と目標復旧レベルを設定し、それを実現する経営資源を特定する、としている（2026年時点）。

```diagram-numbered
title: 重要業務を「時間軸」で並べる
1. 即時（人命安全・安否確認）─ 最優先、ここが揃わないと次に進めない
2. 数時間〜1日（最重要業務の最低限の再開）
3. 数日（重要業務の段階的な復旧）
4. 数週間（通常業務への回帰）
```

「だいたい早く」ではなく、業務ごとに具体的な時間を置く。時間が決まれば、そのために必要な備え（代替・二重化・在庫）の量も決まる。具体の数値は、自社の事業特性と顧客との約束をふまえ、責任者・専門家と定める。


# 第3章 被害想定 ─ 何が、どこを止めるか

## 3.1 原因ではなく「資源の欠落」で考える

被害想定は、災害の種類を数えることではない。

- 地震でも水害でも、結局は「人・拠点・設備・情報・サプライ」のどれかが欠ける
- 原因が違っても、**欠けた資源が同じなら、対応はほぼ同じ**になる
- だから、原因別に無限に備えるのではなく、「どの資源が欠けるか」で考える

これが、被害想定を現実的なサイズに収める考え方だ。原因は無数だが、止まり方には型がある。

## 3.2 五つの主要な脅威と、欠ける資源

主要な脅威を、欠ける資源とともに押さえる。

- **地震**:拠点・設備の損壊、人の被災、交通・物流の麻痺。広域・同時に来る
- **水害（台風・豪雨）**:浸水による拠点・在庫の損失。ある程度予測でき、事前の動きが効く
- **感染症**:人が一斉に欠ける。建物は無事でも、出社できる人がいない
- **大規模システム障害**:情報・通信・決済が止まる。物理的被害はないのに業務が動かない
- **サプライ途絶**:自社は無事でも、仕入れ先・委託先が止まって連鎖する

## 3.3 「自社は無事」でも止まる、を見落とさない

見落としやすいのは、自社に直接の被害がない型だ。

- 感染症は、建物も設備も無傷のまま、人だけが欠ける
- システム障害は、物理的被害ゼロで、業務が完全に止まる
- サプライ途絶は、隣の被災が自社に波及する

「自社の建物が壊れる」想定だけでは、半分しか備えられない。**人が来られない・情報が動かない・モノが届かない**——この三つを、被害想定に必ず入れる。被害の具体的な規模は、自社の立地（ハザードマップ）・取引構造をふまえ、専門家と見積もる。


# 第4章 初動と代替手段

## 4.1 順番を間違えない ─ 人命が最優先

発災直後、何から動くか。順番が決まっていないと、混乱する。

```diagram-flow
人命安全の確保（避難・救護・二次災害の防止）
安否確認（従業員・家族の無事を、定めた手段で把握）
事業の初動（被害把握 → 重要業務の代替手段を起動）
```

- **第一に人命**:どんな重要業務より先。けが人・避難が最優先
- **次に安否確認**:誰が動けるかが分からなければ、復旧の計画が立たない
- **そのうえで事業**:被害を把握し、代替手段に切り替える

内閣府も、指揮命令系統の明確化、安否確認の迅速な実施、情報システムのバックアップ確保を、初動の典型として挙げている（2026年時点）。

## 4.2 安否確認は「手段を決めておく」ことが要

安否確認は、当日にやり方を考えていては間に合わない。

- 連絡手段を、複数あらかじめ決める（電話が不通になる前提で、別の手段も）
- 「誰が・いつまでに・どう報告するか」の型を、平時に共有する
- 連絡が取れない場合の扱い（次の手）まで、決めておく

平時に一度も試していない手段は、本番で動かない。後の第6章の訓練で、必ず一度は通す。

## 4.3 代替手段 ─ 「いつもの道」が断たれた前提で

重要業務を戻すとは、いつものやり方に戻すことではない。**断たれた経営資源を、別の手段で埋める**ことだ。

- **拠点が使えない**:代替拠点、在宅・分散勤務、他拠点への業務移管
- **人が足りない**:多能工化（一人が複数業務を担えるよう平時に育てる）、応援体制
- **設備・在庫が失われた**:予備・分散保管、調達先の複線化
- **情報が止まった**:データのバックアップ（遠隔地）、紙での代替手順
- **仕入れが途絶えた**:代替の調達先、安全在庫

代替手段は、平時に用意しておかなければ、発災時に存在しない。「そのとき探す」では間に合わない。何を代替で埋めるかは、第2章の重要業務とRTOから逆算して決める。


# 第5章 連絡網と意思決定 ─ 誰が決めるか

## 5.1 「誰が決めるか」を、先に決めておく

緊急時に最も詰まるのは、設備でも在庫でもない。**意思決定**だ。

- 平常の決裁ルートは、緊急時には遅すぎる
- 決める人が被災・不在だと、誰も動けなくなる
- 「いつ・誰が・何を発動できるか」を、平時に決めておく

```diagram-layers
foundation: 平時に定める ─ 対策本部の構成・権限・代行順位・発動の基準
top: 発災時に動く ─ 本部設置 → 情報集約 → 意思決定 → 各部門へ指示
```

## 5.2 代行順位 ─ 「次に決める人」を決める

トップが決められない状況を、必ず想定する。

- 意思決定者が被災・不在のとき、次に誰が決めるか（第1代行・第2代行）
- どの権限を、どこまで委譲するか
- 連絡が取れないとき、現場はどこまで自分の判断で動いてよいか

代行順位がないと、「決める人待ち」で全体が止まる。**決められないことが、最大の被害**になりうる。

## 5.3 連絡網は「上下と横」の両方を

連絡網は、安否確認だけのものではない。

- **縦（指示）**:対策本部から各部門へ、決定を流す
- **横（連携）**:部門間で状況を共有する
- **外（対外）**:顧客・取引先・行政・家族への連絡窓口を一本化する

対外連絡を窓口一本にしないと、情報が錯綜し、信用を損なう。「誰が外に向けて話すか」も、平時に決めておく。具体の連絡先・体制は、自社の組織に合わせ、ユーザー追記欄に当てる。


# 第6章 平時の訓練と見直し ─ 作って終わりにしない

## 6.1 BCPは「文書」ではなく「能力」

立派な計画書があっても、動かなければ意味がない。

- 棚に眠るBCPは、被災時に開かれない
- 一度も試していない手順は、本番で機能しない
- BCPの価値は、文書の厚さではなく、**実際に動かせる能力**にある

内閣府も中小企業庁も、方針策定 → 計画 → 運用 → 教育・訓練 → 点検・是正 → 経営層による見直し、というサイクルを繰り返す継続的改善を求めている（2026年時点）。作るのは出発点で、回すことが本体だ。

## 6.2 訓練で「机上の穴」を見つける

訓練の目的は、うまくやることではない。**できないことを、平時に見つける**ことだ。

- 安否確認を実際に流してみる（何分で何割そろうか）
- 代替手段を一度試す（在宅で重要業務が回るか、紙で代替できるか）
- 代行順位で意思決定を回す（トップ不在の想定で動く）

訓練でうまくいかないのは、失敗ではなく収穫だ。本番でなく訓練で穴が見つかったことを、むしろ良しとする。

## 6.3 見直しの引き金を、決めておく

BCPは、一度作れば終わりではない。次の機会に必ず見直す。

- 組織・事業・拠点・取引先が変わったとき
- 訓練で穴が見つかったとき
- 実際に災害・障害を経験したとき（最大の教材）
- 定期点検のとき（年1回など、頻度を決める）

担当者と見直し頻度を決めておかないと、BCPは作った時点で止まり、現実とずれていく。誰が・いつ見直すかを、ユーザー追記欄に当てる。


# 第7章 ありがちな失敗

熟練した管理職でも通る道。気づいて直せればいい。自分の部門は大丈夫だと思い込まないことが、最初の防御だ。

**失敗① 「うちは大丈夫」で、想定しない**

被災経験がないことを、安全の証拠だと取り違える。だが、起きていないのは、まだ順番が来ていないだけかもしれない。根拠のない楽観は、備えをすべて止める。

**失敗② 全部を守ろうとして、何も守れない**

優先順位をつけられず、すべてを重要だとする。結果、限られた資源が分散し、本当に守るべきものが守れない。絞ることが、守ることだ。第2章の問いに戻る。

**失敗③ 立派な文書を作って、満足する**

分厚いBCPが完成した時点で、ゴールにしてしまう。訓練で動かさなければ、それは紙だ。価値は文書ではなく、動かせる能力にある。

**失敗④ 「自社の被害」しか想定しない**

建物が壊れる想定はするが、人が来られない・情報が止まる・仕入れが途絶える、を見落とす。物理的被害ゼロで業務が止まる型を、想定に必ず入れる。

**失敗⑤ 決める人を、一人にする**

意思決定をトップ一人に集中させ、代行順位を決めていない。その一人が被災すれば、全体が止まる。「次に決める人」を、必ず用意しておく。


# 第8章 反復ワーク

## 8.1 自部門の「止まると困る業務」を、三つ書く

自部門の業務から、止まると最も困るものを三つだけ挙げ、書き出す。

- それぞれ、止まると誰に・どんな影響が出るか
- それぞれ、いつまでに戻す必要があるか（おおよその時間でよい）
- 全部を同時に守れないなら、どの順で戻すか

三つに絞ることで、守るべきものが見える。全部を挙げようとすると、また優先順位が消える。

## 8.2 一つの脅威で、欠ける資源をたどる

「感染症で出社できる人が半分になった」と仮定し、自部門で書き出す。

- どの重要業務が、止まるか
- いま、それを埋める代替手段は、あるか／ないか
- ないなら、平時に何を用意しておけば埋まるか

原因を一つ固定して資源の欠落をたどると、自部門の穴が具体的に見えてくる。


# 第9章 ユーザー追記欄

本コースは普遍的原理に絞っている。**自社固有の情報を、ここに当てる**。

- 自社のBCP本体（版・改訂日）と、その保管場所
- 自部門の重要業務一覧と、それぞれの目標復旧時間（RTO）
- 安否確認の手段・ルール（誰が・いつまでに・どう報告するか）
- 代替拠点・代替手段・バックアップの所在
- 対策本部の構成・権限・代行順位（第1・第2代行）
- 連絡網（社内縦横／顧客・取引先・行政／対外窓口）
- 自社の立地リスク（ハザードマップ上の浸水・揺れやすさ等）
- 訓練の予定と、前回訓練で見つかった穴
- 次回見直しの担当者と時期

追記欄が空のままだと、本コースは「立派なテキスト」で終わる。自社の重要業務とRTO、安否確認の手段、代行順位を当てて初めて、明日から動く備えになる。

