わからないことがある。でも、聞けない。みんな忙しそうだ。こんなことを聞いたら、迷惑だ。

そう思って、自分で調べる。時間がかかる。気づけば、もう夕方だ。

——あなたは、真面目だ。

その真面目さが、いま、空回りしている。

学生のころは、それでよかった。ひとりで頑張れば、ひとりぶんの成果が出た。テストも、レポートも、自分の力で完結した。

ところが、仕事は違う。仕事は、ひとりでは終わらない。あなたの仕事は、誰かの仕事につながっている。あなたが止まれば、その先も止まる。

「迷惑をかけたくない」という気持ちは、優しさだ。でも、その優しさが、いちばんの迷惑になることがある。聞かずに進めて、間違える。抱えこんで、納期に間に合わない。誰かに一言かけていれば防げたことが、ひとりで抱えたせいで、大きくなる。

この原稿は、その空回りを止めるためのものだ。

土台にするのは、経済産業省が定めた「社会人基礎力」という枠組み。その中の「チームで働く力」を、評価される項目ではなく、明日から動ける型に作り直す。読み終えたとき、あなたは、ひとりで抱えるのでも、人に頼りきるのでもない、その「あいだ」に立てるようになる。


# 第1章 なぜ、新人はチームで空回りするのか

## 1.1 遠慮と、独り相撲

新人がチームの中でつまずく理由は、たいてい二つに分かれる。

ひとつは、遠慮。

聞きたいのに、聞けない。言いたいのに、言えない。「自分なんかが」と思って、口をつぐむ。会議でも、意見があるのに黙っている。発言して的外れだったら、恥ずかしいから。

もうひとつは、独り相撲。

頼まれてもいないのに、全部ひとりでやろうとする。「できます」と言ってしまう。本当は無理なのに、助けを求めない。それが、責任感だと思っている。

遠慮する人も、独り相撲をする人も、見た目は逆だ。でも、根っこは同じだ。

どちらも、ひとりで完結しようとしている。

## 1.2 「ひとり」のクセが、抜けていない

なぜ、ひとりで完結しようとするのか。

それは、二十年以上かけて、そう訓練されてきたからだ。

学校の評価は、ほとんどが「ひとり」の成果だった。自分のテストの点。自分の提出物。隣の人に聞いたら、カンニングと呼ばれた。困ったら、自分で何とかするのが正しかった。

そのクセが、体に染みついている。だから、職場でも無意識に、ひとりで抱える。

でも、職場のルールは、まるで逆だ。

職場では、聞くことは正しい。頼ることは正しい。ひとりで抱えて間違えるより、聞いて正しく進めるほうが、ずっと評価される。

考えてみてほしい。先輩は、なぜ職場にいるのか。あなたに、聞かれるためでもある。教えるのも、仕事のうちだ。あなたが聞かないと、先輩は自分の役割を、半分果たせない。

聞くことは、迷惑ではない。聞かないことのほうが、ときに迷惑になる。

学校で正解だった動き方が、職場では失点になる。ここに、最初のズレがある。

## 1.3 あなたの仕事は、ひとりの持ち物ではない

もうひとつ、知っておいてほしい構造がある。

あなたの仕事は、あなただけのものではない。

学生のレポートは、あなたの持ち物だった。出来が悪くても、困るのはあなただけ。

仕事は違う。あなたの作った資料を、先輩が会議で使う。あなたの入力したデータを、別の部署が見る。あなたが一日遅れれば、その先の三人が、まとめて遅れる。

仕事は、つながっている。だから、ひとりで抱えて止まることは、自分だけの問題では、すでにない。

「迷惑をかけたくないから聞かない」が、なぜ迷惑になるのか。答えは、ここにある。聞かずに止まったあなたの後ろで、見えない誰かが、もっと困っているからだ。

——では、どうするか。

ひとりをやめて、チームに溶けこめ、という話ではない。次の章で、「チームで働く」とは何かを、定義し直す。


# 第2章 「チームで働く」とは何か

## 2.1 一行で定義する

このコースの土台になる定義を、一行で置く。

> チームで働くとは、自分の力を、誰かの力とつなげることだ。

ひとりの力を捨てることではない。チームに埋もれることでもない。あなたの力は、ちゃんと残る。それを、ひとりの中に閉じこめず、外につなげる。それだけだ。

たとえるなら、電池だ。

電池は、一本でも光る。でも、一本の光は弱い。何本かをつなげると、明るくなる。つなぐための端子が、両側についている。その端子が、これから話す力だ。

あなたという電池の性能は、変えなくていい。変えるのは、端子のつなぎ方だ。

## 2.2 つなぐための、六つの端子

社会人基礎力では、「チームで働く力」を六つの要素に分けている。難しい名前がついているが、中身は単純だ。あなたとチームをつなぐ、六つの端子だと思えばいい。

二つずつ、三組に分けて見ていく。

**伝える・聴く（発信力／傾聴力）**

発信力は、自分の意見をわかりやすく伝える力。聴く力は、相手の意見を丁寧に聴く力。

言葉のキャッチボールだ。投げるのが発信、受けるのが傾聴。どちらか片方だけでは、ボールは続かない。

**合わせる・つかむ（柔軟性／情況把握力）**

柔軟性は、意見や立場の違いを理解する力。情況把握力は、自分と周囲との関係を理解する力。

自分が正しいと思っても、相手には相手の事情がある。それを「合わせる」。いま、この場で自分が何をすべきかを「つかむ」。チームの中での、立ち位置の感覚だ。

**守る・整える（規律性／ストレスコントロール力）**

規律性は、ルールや約束を守る力。ストレスコントロール力は、ストレスの発生源に対応する力。

約束を守ることが、信頼の土台になる。そして、つらいときに自分をつぶさないことが、チームに居続けるための力になる。外との約束を「守る」、自分の内側を「整える」。

このうち、ストレスコントロールだけは、少し性質が違う。他の五つは、相手に向かう力だ。これだけは、自分に向かう力だ。なぜ、自分を整えることが「チームで働く力」なのか。理由は単純だ。あなたがつぶれたら、チームは一人ぶん欠ける。あなたが続けられることが、チームへのいちばんの貢献になる場面が、ある。

## 2.3 六つは、バラバラではない

この六つは、別々の技術ではない。

全部、ひとつの方向を向いている。自分の力を、誰かの力とつなげる。その一点だ。

伝えて、聴く。合わせて、つかむ。守って、整える。

どれも、「自分」と「周り」のあいだに、線をつなぐ動きだ。

——では、その線を、どうつなぐのか。次の章で、明日からできる型にする。


# 第3章 明日からの型 ─ 三組の端子をつなぐ

## 3.1 型の全体像

ここが、このコースの本体だ。

六つの要素を、三組に束ねる。一組ずつ、明日から使える型にしていく。

- 第一の型:伝える・聴く（発信力／傾聴力）
- 第二の型:合わせる・つかむ（柔軟性／情況把握力）
- 第三の型:守る・整える（規律性／ストレスコントロール力）

順番に見ていく。

## 3.2 第一の型 ─ 伝える・聴く

**伝えるコツ:結論から、短く。**

新人の報告は、長くなりがちだ。経緯から話して、最後に結論が来る。聞いている先輩は、いつまで続くのかと、内心ヒヤヒヤしている。

逆にする。結論を、先に言う。

> 新人「Aの件、納期に間に合いそうにありません。理由は二つあります。」

これだけで、相手は身構えられる。何の話で、何が問題か、最初の一文でわかる。詳しい経緯は、そのあとでいい。

伝える力とは、うまく話す力ではない。相手が受け取りやすい順番で出す力だ。話すのが苦手でも、順番さえ整えれば、伝わる。

**聴くコツ:最後まで聴いて、一度返す。**

人の話を聴くとき、新人はよく、途中で自分の考えを挟む。「でも」「いや」と。それで、相手は話す気をなくす。

最後まで聴く。そして、一度、相手の言葉を返す。

> 先輩「この資料、もう少し数字を足してほしいんだ。」
>
> 新人「数字を足す、ということですね。どのあたりの数字でしょうか。」

「ですね」と一度返すだけで、相手は「伝わった」と安心する。聴く力とは、黙ることではない。受け取ったと、相手に見せる力だ。

## 3.3 第二の型 ─ 合わせる・つかむ

**合わせるコツ:正しさで勝とうとしない。**

自分の意見が正しいと思う。先輩の意見は古いと思う。新人なら、誰でも一度は思う。

でも、職場では、正しさで押し切っても、人は動かない。動くのは、納得したときだけだ。

意見が違うとき、まず相手の立場を聞く。

> 新人「なるほど、その進め方には、こういう理由があるんですね。一点だけ、こうしたほうが早いかもと思ったのですが、いかがでしょうか。」

否定から入らない。理解を示してから、提案する。柔軟性とは、自分を曲げることではない。違いを、敵にしない力だ。

**つかむコツ:いまの自分の役割を、口に出して確認する。**

会議でも、共同作業でも、新人がよく迷うのは「自分は、いま何をすればいいのか」だ。わからないまま、固まる。周りは動いているのに、自分だけ、何をしていいかわからない。その時間は、つらい。

わからないときは、聞いていい。

> 新人「いまの作業で、自分が手を動かすべきところは、どこでしょうか。」

役割を聞くことは、無能の証明ではない。チーム全体を見ようとしている証拠だ。

そして、役割がわかったら、自分の周りも見る。隣の人が困っていないか。誰かの手が、止まっていないか。自分のことだけでなく、周りとの関係が見えてくると、動き方が変わる。

情況をつかむとは、空気を読んで黙ることではない。自分の立ち位置を、はっきりさせる力だ。

## 3.4 第三の型 ─ 守る・整える

**守るコツ:できない約束を、最初からしない。**

新人は、頼まれると、つい「できます」と言う。断ると、評価が下がる気がするからだ。

でも、できない約束を守れなかったときのほうが、信頼は崩れる。

無理なときは、その場で正直に言う。

> 先輩「明日までに、これお願いできる?」
>
> 新人「いまBの作業を抱えていて、明日の午前は厳しいです。午後からなら着手できますが、それでも大丈夫でしょうか。」

できる範囲を、先に示す。規律性とは、何でも引き受けることではない。守れる約束だけを、確実に守る力だ。

**整えるコツ:つらさを、ためこまない。**

仕事には、ストレスがつきものだ。叱られる。失敗する。うまくいかない。

新人は、それをひとりで抱えこみやすい。「弱いと思われたくない」から。

でも、ストレスは、ためると爆発する。小さいうちに、外に出す。

信頼できる先輩に、こぼす。同期に、話す。それが難しければ、紙に書き出すだけでもいい。何が、どうつらいのか。言葉にすると、つらさは少し小さくなる。

整える力とは、我慢する力ではない。つぶれる前に、対処する力だ。

——三組の型を、まとめて使う必要はない。明日、ひとつだけ試す。それで十分だ。


# 第4章 ありがちな失敗 ─ そして、どう戻るか

これから挙げる失敗は、どれも、普通に通る道だ。あなたの性格の問題ではない。気づいて、次から戻れれば、それでいい。

## 失敗① ─ 言えない

意見がある。質問がある。でも、言えない。

「こんなことを言ったら、変に思われる」「的外れだったら、恥ずかしい」。そう思って、飲みこむ。

これは、発信力の不足ではない。相手の反応を、こわがりすぎているだけだ。

**戻り方**

最初は、意見でなくていい。質問から始める。「これは、こういう理解で合っていますか」。質問なら、的外れでも誰も困らない。むしろ、的確な質問は、わかっている人だと思われる。

質問が言えるようになると、意見も、少しずつ言えるようになる。声を出すことに、体が慣れていく。沈黙していた人が一言発するだけで、チームの中での存在は、確かに変わる。

## 失敗② ─ 聞きすぎる

逆もある。不安だから、何でもかんでも聞く。

「これでいいですか」「次は何をすればいいですか」。確認のたびに、先輩の手が止まる。気づけば、自分で考えることを、やめている。

これは、傾聴ではない。判断を、相手に丸投げしている。

**戻り方**

聞く前に、自分の答えを一つ持つ。「自分はこうしようと思うのですが、合っていますか」。答えを持って聞くと、相手は「合っている」か「違う」かだけ返せばいい。相手の手間も、減る。あなたも、考える力が育つ。

聞くことと、丸投げすることは、違う。自分の頭を一度通したうえで聞く。それが、頼ることと、寄りかかることの境目だ。

## 失敗③ ─ 抱えこむ

頼まれた仕事が、終わらない。でも、言い出せない。

「もう少しで終わる」「自分が無能だと思われたくない」。そう思って、ひとりで残業する。そして、納期の朝、間に合っていないことが、ばれる。

これが、いちばん危ない失敗だ。早く言えば防げたものが、手遅れになる。

**戻り方**

「間に合わない」は、早いほど価値がある。終わってから言うのは、ただの報告。終わる前に言えば、まだ手が打てる。人を足す。順番を変える。納期を交渉する。早ければ、選べる手がいくつもある。遅ければ、もう何も選べない。

遅れそうだと思った瞬間に、言う。それは、迷惑ではない。誠実だ。

## 失敗④ ─ 合わせすぎる

人に合わせることを、覚えはじめる。すると今度は、合わせすぎる。

自分の意見を、全部ひっこめる。何を聞かれても「大丈夫です」「お任せします」。波風は立たない。でも、あなたがそこにいる意味も、消えていく。

**戻り方**

合わせることと、消えることは、違う。相手を尊重したうえで、自分の考えも一つ残す。「自分はこう思いますが、最終的にはお任せします」。意見を置いてから、委ねる。

あなたがそこにいる意味は、あなたの考えが、たまにでも場に出ることだ。全部を主張する必要はない。でも、ゼロにもしない。それが、あいだに立つということだ。


# 第5章 実践ワークシート ─ いまのあなたの、つながり方

ここからは、手を動かす時間だ。採点ではない。いま自分が、ひとりに寄っているか、チームに溶けすぎているか、それを確かめるための問いだ。

## 【いまの自分を知る問い】

### 問い1:あなたは、どちらに偏っているか

- この一週間で、聞きたいのに聞けなかったことは、いくつあったか。
- 逆に、自分で考えずに聞いてしまったことは、いくつあったか。
- どちらが多いか。あなたは、遠慮の側か、それとも頼りすぎの側か。

### 問い2:六つの端子を、自己点検する

次の六つを、できている順に並べてみる。

- 伝える／聴く／合わせる／つかむ／守る／整える

いちばんできていないのは、どれか。それが、明日いちばん伸びる場所だ。

## 【明日の行動を決める問い】

### 問い3:ひとつだけ、決める

- 明日、ひとつだけ試すなら、どの型か。
- それを、どの場面で、誰に対して使うか。
- うまくいかなくても、それは失敗ではない。試したこと自体が、前進だ。

## 【一週間後に振り返る問い】

### 問い4:何が変わったか

- 試してみて、相手の反応は、どう変わったか。
- 自分の気持ちは、少し楽になったか。
- この型を、次は何のために使いたいと思うか。

最後の問いだけは、急がなくていい。いま答えが出なくても、働きながら、ゆっくり育てればいい。


# おわりに ─ つなぎ方は、一生かけて覚えていく

このコースで、いくつもの型を渡した。

伝える・聴く。合わせる・つかむ。守る・整える。チームで働くための、六つの端子。

明日から、ひとつずつ、試してみてほしい。

ただ、最後に、三つのことを置いておきたい。働くことを、長い目で見るための、三つの視点だ。

ひとつめは、何のためにつながるのか。チームで働く力は、人に好かれるための技術ではない。あなたの力を、もっと大きな何かに届けるための力だ。あなたは、何のために、誰とつながりたいのか。

ふたつめは、つなぎ方は、学び続けられる。いまうまくつなげなくても、当たり前だ。あなたは、まだひとりのクセが抜けていないだけだ。つなぎ方は、技術だ。技術は、何歳からでも伸びる。

みっつめは、ひとりとチームを、統合する。チームで働くからといって、ひとりの力を捨てなくていい。むしろ逆だ。ひとりで立てる人ほど、いいつなぎ方ができる。ひとりとチーム。そのどちらも、あなたの中に持っていていい。

一週間に一度でいい。立ち止まって、振り返ってほしい。今週、自分はどんなふうに、人とつながったか。うまくいったことは何で、つまずいたことは何か。その振り返りが、次の一週間の、あなたを変えていく。

——ひとりで抱えるのも、チームに頼りきるのも、どちらも、まだ途中だ。

あなたが立つのは、その「あいだ」だ。

自分の力を持ったまま、それを、誰かとつなげる場所。

明日、誰かに何かを伝えるとき、まず問うてほしい。

これは、自分ひとりの仕事か。それとも、誰かにつながっているか。

その問いを持てた時点で、あなたはもう、ひとりの成果から、チームの成果へと、歩きはじめている。


