# 第2章 「考え抜く」とは何か ─ 三つの力に分ける

## 2.1 一行で定義する

このコース全体の土台を、一行で置く。

> 考え抜くとは、答えの手前にある「問い」を、自分で立てることだ。

言われたことをやるのは、答えを実行すること。

考え抜くのは、その答えがなぜ必要だったのか、もっといい答えはないのかを、自分で問い直すこと。

抽象的に聞こえるなら、三つに分けるとわかりやすい。社会人基礎力では、考え抜く力を三つの要素に分けている。**課題発見力・計画力・創造力**だ。

順番に見ていく。

## 2.2 課題発見力 ─ 「あれ?」に気づく力

一つめは、課題発見力。

現状をよく見て、「ここに問題がある。解決したほうがいい」と気づき、提案する力だ。

難しく考えなくていい。出発点は、たった一つの感覚だ。

「あれ?」。

なんで毎回これ手書きなんだろう。なんでこの作業、二人でやってるんだろう。お客さんからの同じ質問、今週で三回目じゃないか。

その小さな引っかかりが、課題の入口だ。

こなす人は、引っかかりをスルーする。「そういうものだ」と流す。考え抜く人は、その「あれ?」を捨てない。立ち止まって、なぜそうなっているかを考える。

おもしろいことに、新人だからこそ気づける引っかかりがある。長くいる人は、おかしなやり方にも慣れてしまう。慣れると、見えなくなる。

入ったばかりのあなたの目は、まだ慣れていない。だから、ベテランが見落とす「あれ?」が、よく見える。それは、新人の弱みではなく、武器だ。

## 2.3 計画力 ─ 段取りを描く力

二つめは、計画力。

課題を解くために、複数のやり方を描いて、最善を選び、期限内に準備する力だ。

ここで大事なのは、「複数」という言葉だ。

やり方を一つしか思いつかない人は、計画していない。ただ、思いついた順にやっているだけだ。

計画力のある人は、頭の中で二つ三つの段取りを並べる。「Aの順でやると速いが、抜けが出やすい」「Bの順なら確実だが、時間がかかる」。並べて、比べて、選ぶ。

そして、期限から逆算する。いつまでに何を終わらせれば間に合うか。後ろから線を引く。

計画とは、未来を先に一度、頭の中で生きてみることだ。実際に動く前に、起こりそうなことを想像し、つまずく場所に手を打っておく。

だから、計画している人は、慌てない。想定の中で、すでに一度通った道だからだ。

## 2.4 創造力 ─ 新しいやり方を考える力

三つめは、創造力。

既存の発想にとらわれず、新しい解決方法を考える力だ。

「創造」と聞くと、芸術家のひらめきのように思えるかもしれない。

違う。新人の創造力は、もっと地味だ。

「いつもこうやってる」を、「もっといい方法はないか」と疑うこと。それだけだ。

前任者のやり方をそのまま引き継ぐのではなく、自分の手で一度、組み直してみる。他の部署のやり方を借りてくる。まったく関係ない場面で見たコツを、いまの仕事に応用してみる。

ゼロから生み出さなくていい。組み合わせを変えるだけで、新しい価値は生まれる。

世の中の新しいものの多くは、まったくの無からは生まれていない。すでにあるものと、別のすでにあるものを、これまで誰もつながなかった形でつないだ。それだけだ。

だから、創造力に必要なのは天才のひらめきではない。「これとこれ、組み合わせたらどうだろう」と試す、小さな好奇心だ。

三つを並べると、見えてくる。課題発見は「問いを立てる」。計画は「道を描く」。創造は「道を新しくする」。

考え抜くとは、この三つを順に回すことだ。


# 第3章 明日からの型 ─ 三つの力を、行動にする

## 3.1 型の全体像

ここが、このコースの本体だ。

三つの力を、明日から使える手順にする。

特別な才能はいらない。やることを、五つのステップに分ける。

- ステップ1:作業の前に「なぜ?」を一つ書く（課題発見）
- ステップ2:引っかかりを、メモに残す（課題発見）
- ステップ3:やり方を、二つ以上考えてから動く（計画）
- ステップ4:期限から逆算して、線を引く（計画）
- ステップ5:「もっといい方法」を、小さく一つ試す（創造）

順番に見ていく。

## 3.2 ステップ1 ─ 作業の前に「なぜ?」を一つ書く

指示を受けたら、すぐ手を動かさない。

まず、自分に問う。

> この作業は、なんのためにあるのか。

「この資料、なんのために使うんだろう」。「この数字、誰が、何を判断するために見るんだろう」。

一つでいい。紙の隅か、メモアプリに、書いてみる。

わからなければ、聞けばいい。

> あなた「この資料、お客さまとの打ち合わせで使う感じですか? 用途がわかれば、見やすさを寄せられるので。」

この一言で、あなたは「答えを実行する人」から「目的を理解して動く人」に変わる。先輩は、たいてい喜ぶ。目的を理解しようとする新人は、信頼されるからだ。

## 3.3 ステップ2 ─ 引っかかりを、メモに残す

仕事をしていると、一日に何度か「あれ?」が来る。

たいてい、その場で消える。忙しいから。流れていくから。

その「あれ?」を、捨てない。一行でいいから、残す。

「請求書の確認、毎回二人でやってる。一人でよくないか?」
「お客さんから同じ質問が今週三回。FAQにできないか?」

すぐ提案しなくていい。まず、ためる。

一週間ためると、あなただけの「課題リスト」ができる。それは、誰にも言われていない、あなたが自分で見つけた問いだ。

考え抜く力は、ひらめきではない。引っかかりを捨てない習慣のことだ。

## 3.4 ステップ3 ─ やり方を、二つ以上考えてから動く

作業に入る前に、もう一つ。

やり方を、一つで決めない。

頭の中で、最低二つ並べる。

「先に全部集めてから、まとめて入力する」。「届いた順に、その都度入力する」。

どちらが速いか。どちらがミスしにくいか。今日の自分の状況なら、どちらが合うか。

二つ並べて、選ぶ。たったこれだけで、行き当たりばったりが、計画に変わる。

慣れてくると、数秒でできる。最初だけ、意識して並べてみればいい。

一つしか道がないと思っているとき、人は、その道に縛られる。二つ目を思いついた瞬間に、はじめて「選ぶ」という自由が生まれる。

## 3.5 ステップ4 ─ 期限から逆算して、線を引く

締め切りのある仕事を渡されたら、すぐにやることがある。

ゴールから、逆に線を引く。

金曜の朝に提出なら、木曜の夕方には完成させて、見直す時間を取る。そのためには水曜までに中身を作る。となると、火曜には材料を集め終える。

前から「できるところまで」進めると、終盤で必ず詰まる。

後ろから線を引くと、今日やるべき分量が、はっきり見える。

そして、こう一言、先輩に添えられたら上出来だ。

> あなた「金曜提出なので、木曜の午前に一度、確認してもらえますか? そこで直せるように進めます。」

期限を守る人ではなく、期限から逆算できる人になる。これが計画力だ。

## 3.6 ステップ5 ─ 「もっといい方法」を、小さく一つ試す

最後は、創造力だ。

身構えなくていい。大改革はいらない。

いつもの作業の中で、「もっといい方法はないか」を一つだけ試す。

入力の順番を変えてみる。テンプレートを一つ作ってみる。ショートカットを一つ覚えてみる。

うまくいったら、続ける。だめなら、戻せばいい。小さく試すから、失敗してもダメージがない。新しい価値は、大きな賭けではなく、戻せる小さな実験の数から生まれる。

そして、うまくいったら、共有する。

> あなた「この作業、テンプレートを作ってみたら、半分の時間でできました。よかったら、みんなで使いませんか?」

自分のための工夫が、チームのための価値になる。新しい価値を生むとは、こういう地味な一歩の積み重ねだ。

五つのステップを、毎日全部やる必要はない。今日は一つ。それで十分だ。

大事なのは、数ではなく、一つでも自分の意志で選んで動いたという事実だ。その一つが、明日のあなたの目を、少しだけ変える。


# 第4章 ありがちな失敗 ─ そして、どう戻るか

これから挙げる失敗は、どれも、考え抜こうとする人が必ず通る道だ。あなたの資質の問題ではない。気づいて、次から戻れれば、それでいい。

## 失敗① ─ 考えすぎて、動けなくなる

「ちゃんと考えてから動こう」。

その気持ちが強すぎると、いつまでも動けなくなる。

完璧な計画を立てようとして、計画ばかりが膨らむ。あらゆる可能性を潰そうとして、一歩も踏み出せない。

考えることが、動かない言い訳になっている。

**戻り方**

「七割わかったら、動く」と決める。

考え抜く力は、止まる力ではない。動きながら、考え続ける力だ。完璧な答えは、机の上では出ない。動いて、ぶつかって、はじめて見えてくる。

迷ったら、小さく一歩。それも、考え抜く力の一部だ。

## 失敗② ─ 自己流の空想に走る

逆もある。

「もっといい方法があるはずだ」と思い込んで、現場を見ずに、頭の中だけで案を作る。

実際にやっている人に聞かず、なぜ今のやり方になっているかも調べず、思いついた「改善案」を持っていく。

たいてい、すでに誰かが試して、だめだった案だったりする。

**戻り方**

新しい案を考える前に、まず「いまのやり方の理由」を聞く。

> あなた「これ、いまこの順番でやっているのには、何か理由がありますか?」

理由を知って、それでもなお「変えられる」と思ったときの案だけが、本物の創造だ。空想と創造を分けるのは、現状を見たかどうかだ。

## 失敗③ ─ 気づいたのに、言わない

引っかかりに、気づく。「これ、おかしいな」と思う。

でも、言わない。

新人が口を出していいのか。間違っていたら恥ずかしい。波風を立てたくない。

そうやって、せっかくの「あれ?」を、自分の中で消してしまう。

**戻り方**

「断定」ではなく「質問」で出す。

「これは間違っています」ではなく、「これは、こうしたほうがいいかもと思ったのですが、どうでしょう?」。

質問の形なら、間違っていても角が立たない。合っていれば、いい提案になる。気づきは、出し方を変えるだけで、安全に世に出せる。

## 失敗④ ─ 提案が通らず、心が折れる

勇気を出して提案した。でも、通らなかった。

「今はいい」「前例がない」。そう言われて、一気にやる気が消える。

二度と言うものか、と心が閉じる。

**戻り方**

提案は、一回で通らなくていい。

通らなかった理由を、一つ聞く。「どこが引っかかりましたか?」。その理由が、次の提案を強くする。

通らなかった提案は、失敗ではない。次に通すための、材料だ。考え抜く人は、一度の却下で考えるのをやめない。


# 第5章 実践ワークシート ─ いまの仕事で、考え抜く

ここからは、手を動かす時間だ。いま抱えている仕事を一つ、頭に置いて、紙に書いてみてほしい。

## 【動き出す前の問い】

### 問い1:目的を、言葉にできるか

- いま抱えている仕事は、なんのためにあるか。
- 誰が、それを使って、何を判断するか。
- 目的が言えないなら、誰に、どう聞くか。

### 問い2:やり方を、二つ並べられるか

- この仕事の進め方を、二つ書き出す。
- それぞれの、速さと、ミスのしにくさは。
- 期限から逆算すると、今日やるべき分量は、どこまでか。

## 【動いたあとに振り返る問い】

### 問い3:引っかかりを、捨てなかったか

- 今週、「あれ?」と思った瞬間は、何回あったか。
- そのうち、メモに残したのは、いくつか。
- 一つでも、質問や提案の形で、外に出せたか。

### 問い4:学びを、次にどう活かすか

- 今日やってみて、うまくいったことは何か。
- もっといい方法を、一つでも試せたか。
- その工夫は、自分だけのものか。誰かに渡せるか。

採点のための表ではない。自分がいま、「こなす」だけで止まっていないかを、確かめるための問いだ。

この振り返りを、週に一度でいい。続けると、あなたの中に「考え抜く癖」が残っていく。


# おわりに ─ こなす力の上に、考える力を積む

このコースで、たくさんの型を渡した。

考え抜くことの定義。三つの力。明日からの五つのステップ。失敗からの戻り方。

明日から、そのまま使ってみてほしい。

ただ、最後に一つだけ言っておきたい。

考え抜く力は、一日では身につかない。

そして、その力は、いま目の前の一年だけのものではない。

これから、あなたは長く働く。十年、二十年、もっと先まで。仕事の中身は、何度も変わる。いまの正解が、五年後には古くなる。

そのとき、あなたを支えるのは、何か。

決まった作業を、こなす力ではない。

目の前の変化に「あれ?」と気づき、自分で問いを立て、新しい道を描ける力だ。考え抜く力は、変わり続ける時代を、自分の足で歩くための力なのだ。

だから、いまから少しずつ積んでいけばいい。

今日やった仕事を、一つだけ振り返る。「もっといい方法はなかったか」。「あの引っかかりは、なんだったのか」。

その振り返りが、明日のあなたの目を、少し鋭くする。

——こなす力は、土台だ。捨てなくていい。

その土台の上に、考える力を、一段ずつ積んでいく。

明日、何か一つの仕事を渡されたとき、手を動かす前に、まず問うてほしい。

これは、なんのためにあるのか。

その問いを持てた時点で、あなたはもう、言われたことの、その先を見ている。


