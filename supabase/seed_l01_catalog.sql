# 第2章 「前に踏み出す」とは何か ─ 三つの力に分ける

## 2.1 一行で定義する

このコースの土台になる定義を、一行で置く。

> 前に踏み出すとは、指示を待つ前に、自分から一歩を出すことである。

ただし、これは「勝手に突っ走れ」という意味ではない。やみくもに動くことと、自分で動くことは、まったく違う。

前に踏み出す力は、経済産業省が定めた「社会人基礎力」という枠組みの中の、ひとつ目の力だ。難しそうな名前がついている。だが、中身は、三つのシンプルな動きに分けられる。

ひとつずつ、平易な言葉に置き換えていく。

## 2.2 一つ目 ─ 主体性（自分でやることを見つける）

**主体性**とは、指示を待たず、自分でやるべきことを見つけて取り組む力だ。

「何かやることありますか」と聞きに行くのが、指示待ち。

「いま手が空いたので、あの資料の整理を進めておきます」と言えるのが、主体性。

違いは、たったひとつ。仕事を「もらう」のか、仕事を「見つける」のか。

見つけるためには、周りを観察するしかない。先輩は何に困っているか。チームの中で、誰の手も回っていないことは何か。それを見ようとする目が、主体性の出発点だ。

## 2.3 二つ目 ─ 働きかけ力（周りを動かす）

**働きかけ力**とは、自分ひとりで抱えず、周りの人に「やろう」と声をかけて巻き込む力だ。

新人は、つい、ひとりで抱え込む。聞くのが申し訳ない。迷惑をかけたくない。だから、黙って、ひとりでやろうとする。

だが、仕事は、ひとりで完結しない。

「これ、一緒に確認してもらえますか」と頼る。「こうしたほうがよくないですか」と提案する。それも、立派な働きかけだ。

働きかけ力は、声が大きい人の特権ではない。小さな声でも、人を動かす言葉はある。

## 2.4 三つ目 ─ 実行力（失敗を恐れず、やり切る）

**実行力**とは、自分で目標を立て、失敗を恐れず、粘り強く最後までやり切る力だ。

ここで大事なのは、「失敗を恐れず」という部分だ。

新人の失敗は、織り込み済みだ。誰も、あなたに完璧を期待していない。むしろ、何もせずに止まっているほうが、まわりは困る。

実行力とは、走り出すことと、転んでも立ち上がって走り続けること。この二つをセットで指す。

主体性で「見つけ」、働きかけ力で「巻き込み」、実行力で「やり切る」。

この三つが、前に踏み出す力の中身だ。三つを並べると、よくわかる。どれも、特別な才能ではない。明日から、行動として始められるものばかりだ。


# 第3章 明日からの型 ─ 三つの力を、行動にする

## 3.1 型の全体像

ここが、このコースの本体だ。

三つの力を、それぞれ「明日からできる行動」に落とす。順番に見ていく。

- ステップ1:朝、自分で「今日の一歩」を決める（主体性）
- ステップ2:手が空いたら、聞く前にひとつ動く（主体性）
- ステップ3:抱え込まず、声に出して頼る・提案する（働きかけ力）
- ステップ4:小さく始めて、失敗を報告に変える（実行力）
- ステップ5:一日の終わりに、動けたかを振り返る（実行力）

特別な道具は要らない。今日の自分の仕事の中で、すぐ試せる。

## 3.2 ステップ1 ─ 朝、自分で「今日の一歩」を決める

出社したら、指示をもらう前に、自分に問う。

> 今日、自分から進められることは、何か。

ひとつでいい。たとえば「昨日やり残した入力を、午前中に終わらせる」。それを、頭の中で決める。

決めるだけで、一日の立ち位置が変わる。指示を待つ受け身の一日が、自分で始める一日になる。

この「決める」という行為が、なぜ効くのか。

人は、自分で決めたことには、責任を持つ。誰かに決められたことより、最後までやろうとする。

小さくていい。大きな目標を立てる必要はない。「今日の一歩」を、自分で決める。それが主体性の最初の練習だ。

## 3.3 ステップ2 ─ 手が空いたら、聞く前にひとつ動く

仕事が一段落して、手が空いた。

ここが、分かれ道だ。

指示待ちの人は、「何かありますか」と聞きに行く。自分で動ける人は、聞く前に、まず周りを見る。

先輩の机を見る。共有のタスクを見る。誰の手も回っていないことを探す。

そのうえで、こう言う。

> あなた「いま手が空きました。○○の準備を進めておいてもいいですか。」

ポイントは、「何をしましょうか」ではなく、「○○をやってもいいですか」と聞くことだ。

前者は、相手に考えさせる。後者は、自分で考えてから確認している。同じ「確認」でも、まるで違う。

確認するのは、勝手な暴走を防ぐためだ。自分で見つけて、確認してから動く。これが、新人の正しい主体性だ。

## 3.4 ステップ3 ─ 抱え込まず、声に出して頼る・提案する

仕事を進めていて、詰まった。わからないことが出てきた。

このとき、新人がやりがちな失敗がある。ひとりで延々と悩むことだ。

三十分悩んで進まないなら、聞いたほうがいい。だが、ただ「わかりません」と投げるのは、働きかけではない。

働きかけには、型がある。

> あなた「○○の進め方で詰まっています。自分ではAの方法かと思ったのですが、合っていますか。」

自分なりに考えた案を、ひとつ添える。それから聞く。

これだと、相手は答えやすい。あなたが丸投げしていないことも、伝わる。

提案も同じだ。「こうしたほうがいいと思います」ではなく、「こうすると、ここが楽になると思うのですが、どうですか」と問いの形にする。

頼ることも、提案することも、人を動かす行為だ。小さな声でいい。声に出す。それが働きかけ力だ。

## 3.5 ステップ4 ─ 小さく始めて、失敗を報告に変える

自分で見つけ、巻き込んだ。次は、やり切る番だ。

実行力で大事なのは、完璧を目指さないことだ。

新人が止まる原因の多くは、「ちゃんとできる自信がないから、手をつけられない」だ。

だから、小さく始める。全部を一度にやろうとしない。最初の一割だけ、まずやってみる。

そして、失敗したら——隠さない。

> あなた「○○を進めたのですが、ここで間違えてしまいました。やり直す前に、確認させてください。」

失敗を、報告に変える。これが、実行力のいちばん大事な部分だ。

失敗を隠す人は、信頼を失う。失敗をすぐ報告する人は、むしろ信頼される。「この人は、ごまかさない」と。

転んでいい。立ち上がって、報告する。それを繰り返す人が、最後までやり切れる人になる。

粘り強さとは、転ばないことではない。転んでも、また立つことだ。その回数だけ、あなたは前に進む。一度も転ばない人より、転んで立った人のほうが、遠くまで行ける。

## 3.6 ステップ5 ─ 一日の終わりに、動けたかを振り返る

退社する前に、三十秒でいい。自分に問う。

> 今日、自分から動けた場面は、あったか。

あったなら、それを覚えておく。なかったなら、明日は何を変えるかを、ひとつ決める。

この振り返りが、効く。

人は、振り返らないと、同じ一日を繰り返す。昨日と同じように指示を待ち、同じようにうしろめたくなる。

一日の終わりに、自分の動きを点検する。それだけで、明日の一歩が、少し前に出る。

振り返りは、自分を責めるためではない。明日の自分に、バトンを渡すためにある。


# 第4章 ありがちな失敗 ─ そして、どう戻るか

これから挙げる失敗は、どれも、普通に通る道だ。あなたの資質の問題ではない。気づいて、次から戻れれば、それでいい。

## 失敗① ─ 「自分で動く」を、暴走と勘違いする

「自分から動け」と言われて、勢いづく人がいる。

確認もせず、よかれと思って勝手に進める。結果、やり方が違っていて、大きなやり直しになる。

これは、主体性ではない。ただの暴走だ。

自分で動くことの中には、「確認する」が必ず含まれている。見つけて、確認して、動く。この順番を飛ばすと、空回りする。

**戻り方**

動く前に、ひと言確認する癖をつける。「○○を進めてもいいですか」。たったこれだけで、暴走は防げる。確認は、主体性の反対ではない。主体性の一部だ。

## 失敗② ─ 空回りして、「やってる感」だけになる

動くこと自体が目的になってしまう失敗もある。

あれもこれもと手を出す。忙しそうに動き回る。だが、肝心の仕事が前に進んでいない。

「やってる感」はある。だが、成果がない。

これは、目的を見ていないから起きる。前に踏み出すとは、闇雲に動くことではない。「何のために動くか」が抜けると、ただ疲れるだけになる。

**戻り方**

第3章のステップ1に戻る。朝、「今日の一歩」を、ひとつに絞る。動く前に、目的を確かめる。動く量ではなく、進んだ距離で、自分を見る。

## 失敗③ ─ 自己流に走って、人に聞かなくなる

少し慣れてくると、逆の失敗が出る。

自分で動けるようになった。それが過ぎて、何でもひとりで決めるようになる。人に聞かない。相談しない。

これは、主体性が、孤立に変わった状態だ。

働きかけ力が抜け落ちている。自分で動くことと、ひとりで抱えることは、違う。

**戻り方**

ステップ3に戻る。詰まったら、案を添えて聞く。提案を、問いの形でぶつける。自分で動く人ほど、人を巻き込む。ひとりで動く人は、いずれ行き詰まる。

## 失敗④ ─ 一回失敗して、また指示待ちに戻る

自分から動いてみた。だが、失敗した。先輩に注意された。

それで、心が折れる。「やっぱり、言われたことだけやろう」と、指示待ちに戻る。

これが、いちばん多い失敗だ。

だが、考えてほしい。その失敗は、あなたが前に踏み出した証拠だ。動かなければ、失敗もしない。

注意されたのは、あなたの人格ではない。今回のやり方だ。その二つを、混ぜないことだ。やり方は、次から変えられる。

**戻り方**

失敗を、振り返りの材料にする。何が原因だったか。次は何を変えるか。それを、ステップ5の振り返りで言葉にする。一回の失敗で戻るのは、もったいない。失敗は、前に進んだ人だけが手にできるものだ。


# 第5章 実践ワークシート ─ 自己診断と、明日の一歩

ここからは、手を動かす時間だ。紙とペンを用意して、いま現実の自分について、書いてみてほしい。

採点ではない。いまの自分が、どちら側に立っているかを確かめるための問いだ。

## 【自己診断 ─ いまの自分を知る】

次の問いに、正直に答える。当てはまるなら○、半分なら△、当てはまらないなら×。

- 朝、指示をもらう前に、自分で進めることを決めている。
- 手が空いたとき、聞く前に、まず周りを見て動いている。
- 詰まったとき、自分の案を添えて、人に相談できている。
- 失敗を、隠さずにすぐ報告できている。
- 一日の終わりに、自分の動きを振り返っている。

○がいくつあったか。数えてみる。

数が少なくても、落ち込む必要はない。いま、どこに伸びしろがあるかが、見えただけだ。

むしろ、×がついた項目こそ、明日の自分が伸びる場所だ。できていないことに気づけた人だけが、変われる。

## 【明日の一歩 ─ 行動目標を立てる】

×や△がついた項目から、ひとつだけ選ぶ。

- 明日、自分が変える行動は、何か。
- それは、三つの力（主体性・働きかけ・実行）の、どれにあたるか。
- それを、いつ、どの場面でやるか。具体的に書く。

ひとつでいい。欲張らない。

ひとつの行動を、明日、確実にやる。それが、指示待ちから自分で動く人への、最初の一歩になる。

## 【振り返りの問い ─ 一週間後の自分へ】

この問いは、いまは答えなくていい。一週間後に、もう一度、ここに戻ってくる。

- 自分から動けた場面は、あったか。あったなら、何が起きたか。
- そのとき、まわりの反応は、どう変わったか。
- 自分は、何のために、前に踏み出したかったのか。

最後の問いだけは、いまの時点で、少し考えておいてほしい。

あなたは、何のために、自分で動ける人になりたいのか。

その答えが、これから何を学ぶかを決めていく。


# おわりに ─ 三つの問いを、持ち帰る

このコースで、いくつかの型を渡した。

前に踏み出す力の三つの中身。明日からの五つのステップ。失敗からの戻り方。

明日から、そのまま使ってみてほしい。

ただ、最後に、三つの問いを置いておきたい。これは、今日だけの問いではない。これから何年も、あなたの中で効き続ける問いだ。

ひとつ目。**あなたは、何のために動くのか**。

前に踏み出すこと自体が、目的ではない。その先に、なりたい自分や、果たしたい役割がある。目的が見えている人の一歩は、ぶれない。

ふたつ目。**あなたは、何を学ぶのか**。

指示待ちのままなら、与えられたことしか学べない。自分で動く人は、自分の足りないところに、自分で気づける。動くことが、学ぶことの入口になる。

みっつ目。**あなたは、それをどう組み合わせるのか**。

主体性も、働きかけも、実行も、ばらばらでは力にならない。見つけて、巻き込んで、やり切る。三つをつなげて、はじめて前に進む。

——指示を守ることは、これからも大事だ。

そのうえで、指示の半歩先に、自分の一歩を足す。それが、あいだに立つということだ。

明日、出社したら、まず問うてほしい。

今日、自分から進められることは、何か。

