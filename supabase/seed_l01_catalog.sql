# 第1章 なぜ、言われたことだけでは止まるのか

## 1.1 「指示」は、安全だ

新人にとって、指示はありがたいものだ。

何をすればいいか、はっきりしている。やれば、終わる。終われば、評価される。

迷わなくていい。間違えても、「言われた通りにやりました」と言える。

——だから、指示の中だけにいると、楽だ。

楽だから、人はそこに居続けようとする。これは、あなたの怠けではない。指示待ちは、不安な新人にとって、いちばん安全な場所なのだ。

## 1.2 指示の中には、「答え」しかない

ここに、見落とされがちな構造がある。

指示というのは、誰かがすでに考え抜いた結果だ。

「この資料を、明日までに作って」という一言の裏には、先輩の頭の中に長い思考がある。なぜこの資料が要るのか。何のために使うのか。どこに問題があって、それをどう解こうとしているのか。

その思考を、ぜんぶ省いて、最後の「結論」だけが、あなたに手渡される。

あなたが受け取っているのは、答えだけだ。

答えだけを実行していると、何が起きるか。

考える筋肉が、育たない。問題を見つける目も、段取りを描く力も、新しいやり方を思いつく頭も、使われないまま眠っている。

たとえるなら、地図の「ここに行け」という矢印だけを渡されている状態だ。なぜそこなのか、他に道はないのか、近道はないのか。それを考える機会が、矢印に隠れて見えない。

矢印をたどるのは、たしかに速い。迷わずに済む。

ただ、矢印を出した人がいなくなったら、あなたは動けなくなる。自分で道を読む経験を、積んでこなかったからだ。

## 1.3 「こなす人」は、いつか追い抜かれる

最初の一年は、こなせるだけで通用する。

差は、二年目、三年目で開きはじめる。

同期の誰かが、こんなことを言い出す。「これ、毎回手作業でやってますけど、自動化できませんか」。「この資料、お客さん向けには順番を変えたほうが伝わると思います」。

言われたことは、二人とも同じようにできる。

違うのは、言われていないことに気づけるかどうか、だ。

その差は、能力の差ではない。目の向け方の差だ。片方は、渡された答えだけを見ている。もう片方は、答えの手前にある「問い」を見ている。

そして、この差は、放っておくと毎日少しずつ開いていく。一日ぶんは、ほんのわずかだ。けれど、一年で二百日以上が積み重なる。

気づいたときには、追いつけない距離になっている。逆に言えば、いまから目の向け方を変えれば、その積み重ねは、あなたの側につく。

——では、その「問い」を見る目は、どうすれば持てるのか。

